<p><span><em>All Star Comics</em> #8</span> (Winter, 1941) featured the first appearance of 
			<a href="../heroes/dc/WonderWomanI.php" target="_blank">Wonder Woman</a>.</p><br />

			<p>Although not the first female superhero, Wonder Woman is the most popular of the early female superheroes. She 
			was created by psychologist Dr. William Moulton Marston, a feminist who is also credited for his contributions to 
			the invention of the polygraph, or as it is commonly known, the lie detector. One of the powers Marston bequeathed 
			to Wonder Woman was the ability to force others to tell the truth when bound with her magic lasso.</p><br />