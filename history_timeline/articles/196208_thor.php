<p><span><em>Journey into Mystery</em> #83</span> (Aug. 1962) featured the first comic book appearance of 
			<a href="../heroes/marvel/Thor.php" target="_blank">Thor</a>, the Norse god of thunder.</p><br />
			
			<p>Marvel Comic's head writer and Editor-in-Chief, Stan Lee, reasoned that many comic book superheroes possessed 
			god-like abilities, so it would be logical to transform an actual god from ancient mythology into a superhero.</p><br />

			<p>Thor spoke in Shakespearian prose and battled against aliens, criminals and other gods, particularly his adopted brother, 
			the trickster god <a href="../villains/marvel/Loki.php" target="_blank">Loki</a>.</p><br />