<p><span><em>Maus: A Survivor's Tale</em></span> (1972) told the story of the author's parents' survival of the Holocaust in 
			Nazi occupied Poland during World War II.</p><br />
			
			<p>Art Spiegelman used "funny animals" as a means to relate the story of his parents' struggle for survival during 
			World War II. The Jews were mice and the Nazis cats, with other animals representing different nationalities. Published 
			through multiple independent comics and magazines, the tale was collected into two volumes starting in 1986. 
			<em>Maus</em> has been nominated for and won multiple literary awards since being collected into book form.</p><br />