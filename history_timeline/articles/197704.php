<p><span><em>Heavy Metal Magazine</em> #1</span> (April, 1977) introduced American readers to comics from Europe.</p><br />
			
			<p>First published in France in December, 1974 under the title <em>M&#233;tal Hurlant</em>, translated as "Howling Metal," 
			the glossy magazine with an emphasis on science fiction, fantasy and erotica was brought to the United States under 
			the title <em>Heavy Metal</em>. Oriented towards older readers, <em>Heavy Metal</em> was primarily sold on magazine 
			racks and its popularity spawned imitations from other publishers during the 1970's.</p><br />