<p><span><em>Showcase</em> #4</span> (Sept-Oct. 1956) featured the first appearance of the Silver-Age 
			<a href="../heroes/dc/FlashII.php" target="_blank">Flash</a>.</p><br />

			<p>After World War II, comics began to take a downturn in popularity and many publishers went out of business 
			before the 1950s. During the 1950's most publishers turned to genres other than superheroes. In a constant 
			quest to stay viable, publishers printed comics about crime, cowboys, monsters, romance and war. Whatever 
			genre was popular at the moment.</p><br />

			<p>DC Comics mainstays, <a href="../heroes/dc/Superman.php" target="_blank">Superman</a> and 
			<a href="../heroes/dc/BatmanI.php" target="_blank">Batman</a> took on a more science-fiction tone than two-fisted superhero action.
			Timely Comics attempted to revive Golden-Age characters 
			<a href="../heroes/marvel/CaptainAmericaI.php" target="_blank">Captain America</a>, the 
			<a href="../heroes/marvel/HumanTorchI.php" target="_blank">Human Torch</a> and the 
			<a href="../heroes/marvel/SubMariner.php" target="_blank">Sub-Mariner</a>, 
			but none lasted more than a few issues. It was not until 1956 that superheroes would return to prominence when 
			a new take on the golden-age speedster the <a href="../heroes/dc/FlashI.php" target="_blank">Flash</a> was introduced.</p><br />