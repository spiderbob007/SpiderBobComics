<p><span>Vertigo Comics</span> is a publishing imprint of DC Comics that was created to attract older readers with 
			mature stories typically in the horror and fantasy genres.</p><br />

			<p>In many regards, Vertigo Comics filled the void left by Epic Comics when Marvel discontinued the imprint due to 
			financial troubles. Similar to Epic, many of the titles published by Vertigo Comics allow creators to tell darker 
			stories with existing characters owned by the publisher, as well as have a distribution mechanism for creator-owned 
			projects.</p><br />