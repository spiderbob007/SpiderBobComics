<p><span><em>Teenage Mutant Ninja Turtles</em> #1</span> (May, 1984) featured the first comic book appearance of the 
			<a href="../teams/indi/TMNT.php" target="_blank">Teenage Mutant Ninja Turtles</a>, a quartet of anthropomorphic turtles, named after 
			four European Renaissance artists, trained in the assassins' art of Ninjitsu, by an anthropomorphic rat sensei named 
			Splinter.</p><br />

			<p>A one-shot independent comic created by friends Kevin Eastman and Peter Laird over the dinner table; the 
			<em>Teenage Mutant Ninja Turtles</em> were intended to parody elements of popular comics of the early 1980's. Using 
			money from their tax refunds and a loan from Eastman's uncle the two creators were able to 
			self-publish a single comic.</p><br />

			<p>Eastman and Laird saw commercial success when they were approached by a licensing agent, who marketed their 
			characters for action figures and a cartoon to help sell the toys. The Teenage Mutant Ninja Turtles have 
			appeared in multiple syndicated cartoons and several feature films.</p><br />

			<p><em>Teenage Mutant Ninja Turtles</em> is considered by many to be the most successful independent comic in history.</p><br />