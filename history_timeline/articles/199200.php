<p><span>Image Comics</span> is an independent comic book publisher founded primarily by freelance artists who 
			defected from Marvel Comics when they became dissatisfied by the publisher capitalizing on their creative works 
			without paying them royalties.</p><br />

			<p>Image Comics is an umbrella organization for several artists' studios, operating with the understanding that 
			each creator retains ownership of their creations and have total editorial control of the comics they publish.</p><br />

			<p>Although some of the original studios split from Image Comics, the publisher has obtained new creators and 
			continues to be a viable publisher decades after its founding.</p><br />