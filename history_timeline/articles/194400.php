<p>Founded in 1944, <span>Educational Comics</span> published comics about science, history, and the <em>Bible</em> 
			primarily marketed to schools.</p><br />

			<p>After the death of EC Comics' founder Max Gaines in a boating accident, his son Bill Gaines inherited 
			the publishing business. Educational Comics had been losing money, so Bill Gaines changed the focus of 
			the publisher's content to science fiction, crime, horror, and political satire. Changing the company's 
			name to Entertaining Comics, Gaines allowed his writers and artists greater freedom, eventually leading 
			to controversy involving the Congress of the United States.</p><br />