<H1><font face="Comic Sans MS"><IMG height=250 src="dial1.gif" width=250 
align=right></font></H1><font face="Comic Sans MS">Dial 'H' for HERO was always a fun and interesting concept. A 
teen-ager would dial H-E-R-O on a magic H-Dial and transform himself or herself 
into a randomly selected super-hero. The saga of the H-Dial began with Robby 
Reed, way back in late 1965. Robby Reed lived in Littleville, Colorado with his 
grandfather and their housekeeper Miss Millie. Robby was a science prodigy, with 
a well-stocked lab in the rear of his house. One day, he was out at Valley Ridge 
with some friends, when suddenly he fell into a subterranean cavern. He 
discovered a telephone-like dial made of a peculiar alloy, with a strange 
inscription on it. The dial was the handiwork of extraterrestrials. Robby took 
the dial home and deciphered the inscription. He learned that, by dialing 
H-E-R-O, he could become a randomly chosen super-hero. He reverted to normal by 
dialing O-R-E-H. Since he was billed as "The boy who can change into 1,000 
super-heroes", one must wonder if "one thousand" was a true limit. Robby 
disappeared for a while, returned briefly in 1976, then faded into 
obscurity.<BR><BR>
</font>
<H1><font face="Comic Sans MS"><IMG height=300 src="dial2.gif" width=340 
align=left></font></H1><font face="Comic Sans MS">Fifteen years after the debut of the original feature, a new 
series began. This one however did not mention Robby Reed. Christopher King had 
just moved with his family to a New England town named Fairfax, into what was 
rumored to be a haunted house. One day, he went exploring in the attic with 
Victoria Grant, a fellow student at Hamilton Junior High School. The two teens 
found an old chest which contained a wristwatch and a watch pendant. They 
quickly learned that the faces concealed H-Dials. These dials differed slightly 
with the original, Robby's had ten dial holes while these had only four, but the 
concept was still the same. These new dials however had a one hour time limit. 
If either dial was in use, the other would glow to signal the wearer that he or 
she may be needed. They later learned that the dials tapped into the vivid 
imagination of Nick Stevens, a schoolmate of theirs, in order to generate their 
heroic identities. In one of their last recorded adventures, Chris and Vicki 
learned the origin of their dials. A few years back, Robby Reed was in one of 
his super-hero identities, when he became trapped by a super-villain. He dialed 
D-I-V-I-D-E, which transformed him into two entities: the good Wizard and the 
evil Master. The Wizard created the two new H-Dials, while the Master plagued 
the teens behind-the-scenes. The two entities ultimately merged back together, 
and Robby, tired of the role of hero, gave his dial to the kids' friend Nick 
Stevens.<BR><BR>Some time later, Chris and Vicki returned, but not in the way 
anyone would have expected. The two had gone away to college and drifted apart. 
Vicki had gotten in with a bad crowd and was corrupted by the cult known as the 
Children of the Sun. Eventually, Vicki came after Chris, with the desire to kill 
him. Chris went to his house looking for his dial, but found it was missing. He 
ran from Fairfax, and finally contacted the New Teen Titans for help. After an 
intense battle, during which it was learned that Vicki was beginning to store 
residue H-Dial energy, Vicki disappeared. Chris later learned that he too had 
absorbed H-Dial energy, and began to change from hero to hero against his will. 
Later still, Chris was abducted by the Wildebeest, an enemy of the 
Titans.</font><p><font face="Comic Sans MS"><BR><IMG height=320 src="dial3.gif" width=130 
align=left></font></p>
&nbsp;<p><font face="Comic Sans MS">Recently, the teen named Hero Cruz found Vicki's H-Dial 
among the artifacts stolen by the Scavenger. After some help from his friends 
the Ravers, Hero got to keep the dial. Soon after he acquired it however, Vicki 
Grant came looking for it. After a nasty fight, Vicki was subdued and returned 
to normal.<BR><BR><BR><BR><BR><BR>
</font>
