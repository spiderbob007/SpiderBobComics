<p><font face="Comic Sans MS">Although that brings the story of the dial 
"up-to-date", that's not where the story ends! During a weird temporal mishap, 
the 30th century Legion of Super-Heroes found themselves briefly in the 25th 
century. While there, one of their friends, Lori Morning, was secretly given 
Robby Reed's H-Dial by the Time Trapper, which he had stolen from its display at 
the Space Museum. Lori currently possesses the dial in her new home in the 30th 
century. When last seen, the dial had been shorted out and no longer appeared to 
be functional.<BR><BR><BR><BR>That *is* where the story ends! However, for your 
