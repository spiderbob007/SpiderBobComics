			<td class="white_cell">
				<p>Kryptonite is a radioactive element native to the extinct planet 
				<a href="../../locations/dc/Krypton.php" target="_self">Krypton</a>, 
				and depending on its color qualities, effects Kryptonians in different ways.</p><br>
				
				<p>Green Kryptonite is deadly to Kryptonians, such as 
				<a href="../../heroes/dc/Superman.php" target="_self">Superman</a>, 
				and other lifeforms after extended exposure. It has been known to cause cancer in humans after prolonged exposure.</p><br>
				
				<p>Kryptonite radiation is absorbed by Kryptonians, causing blood poising, weakness and eventually death after the skin becomes green.</p><br>
				
				<p>Green Kryptonite is beneficial to Daxamites, such as 
				<a href="../../heroes/dc/M'Onel.php" target="_self">M'Onel</a>, 
				by protecting them from the effects of lead poisoning.</p><br>
				
				<p><span class="bold">Note:</span> After the <a href="../../events/dc/CrisisOnInfiniteEarths.php" target="_self">Crisis on Infinite Earths</a> 
				Kryptonite was reduced to two varieties; Green, which comes from Krypton, and Red, which is man-made.</p><br>
				
				<p>Before the Crisis, there were eight forms of Kryptonite, which are listed in order of appearance.</p><br>
				
				<span class="left">
					<p>1. <span class="bold">Green Kryptonite</span> has the same color as its post-Crisis counterpart, but is only deadly to superpowered Kryptonians. 
					Its radiation is not even cancerous to humans. (<a href="../../issues/dc/1stGreenKryptonite.php" target="_self">Superman Vol. 1 #61</a> (Dec. 1949))</p><br>

					<p>2. <span class="bold">Red Kryptonite</span> is Green Kryptonite which was altered by a dust cloud in space of unknown properties. Its effects 
					are unpredictable and cause Kryptonians to either mutate physically or undergo radical personality changes. The effects last approximately 24-48 
					hours and the same piece of Red K rarely has an effect on the same Kryptonian more than once. 
					(<a href="../../issues/dc/1stRedKryptonite.php" target="_self">Adventure Comics #255</a> (Dec. 1958))</p><br>
					
					<p>3. <span class="bold">Anti-Kryptonite</span> which is green, like Kryptonite, but is only deadly to non-superpowered Kryptonians. 
					(<a href="../../issues/dc/1stAnti-Kryptonite.php" target="_self">Action Comics #252</a> (May, 1959))</p><br>
					
					<p>4. <span class="bold">X-Kryptonite</span> is also green, but it is man-made and empowers non-superpowered beings with superpowers. 
					(<a href="../../issues/dc/1stXKryptonite.php" target="_self">Action Comics #261</a> (Feb. 1960))</p><br>
					
					<p>5. <span class="bold">Blue Kryptonite</span> is an imperfect form of Green Kryptonite duplicated with a Bizarro Ray. It only has an effect on 
					imperfect duplicates of superpowered Kryptonians such as <a href="../../villains/dc/Bizarro.php" target="_self">Bizarro</a>. 
					(<a href="../../issues/dc/1stBlueKryptonite.php" target="_self">Superman Vol. 1 #140</a> (Oct. 1960))</p><br>
					
					<p>6. <span class="bold">White Kryptonite</span> is deadly to plant life, regardless of its planet of origin. 
					(<a href="../../issues/dc/1stWhiteKryptonite.php" target="_self">Adventure Comics #279</a> (Dec. 1960))</p><br>
					
					<p>7. <span class="bold">Gold Kryptonite</span> is as its name implies gold and although not deadly, permanently robs superpowered Kryptonians 
					of their abilities. (<a href="../../issues/dc/1stGoldKryptonite.php" target="_self">Adventure Comics #299</a> (Aug. 1962))</p><br>
					
					<p>8. <span class="bold">Jewel Kryptonite</span> are gems chemically altered, preceding the destruction of Krypton, which further altered the 
					minerals. Jewel Kryptonite empowered individuals trapped within the other dimensional Phantom Zone with telepathic powers to control events 
					outside of the dimension. (<a href="../../issues/dc/1stJewelKryptonite.php" target="_self">Action Comics #310</a> (March, 1964))</p><br>
					
					<p>9. <span class="bold">Magno Kryptonite</span> is a man-made element which adheres magnetically to any substance from the planet 
					<a href="../../locations/dc/Krypton.php" target="_self">Krypton</a>. 
					(<a href="../../issues/dc/1stMagnoKryptonite.php" target="_self">Superman's Pal, Jimmy Olsen #92</a> (April, 1966))</p><br>
					
					<p>10. <span class="bold">Kryptonite Plus</span> is a super-powerful isotope which is fatal to Kryptonians in a short period of time. 
					(<a href="../../issues/dc/1stKryptonitePlus.php" target="_self">Action Comics #350</a> (May, 1967))</p><br>
