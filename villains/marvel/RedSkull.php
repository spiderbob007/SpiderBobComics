			shopkeeper who employed him. Refusing his love, Shmidt killed the young woman in a fit of rage and fled. He felt ecstatic 
			joy in committing the murder and giving vent to his pent rage at the world.</p><br>
			
			<p>When the Nazis took power, Shmidt was working as a bellboy at the hotel where Adolf Hitler was a guest. Hitler was 
			berating a Gestapo officer when the bellboy delivered the Fuhrer's refreshments. Hitler claimed that he could teach the 
			bellboy to do a better job than the officer. Shmidt saw in Hitler the mentor he had always desired, and Hitler recognized 
			in the young man a hatred for humanity that he recognized in himself.</p><br>
			
