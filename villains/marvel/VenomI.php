	<tr>
		<td class="white_cell">
			<p><a href="../../heroes/marvel/SpiderManI.php" target="_self">Spider-Man</a> came in contact with an alien 
			symbiote on the Beyonder's Battle World during the 
			<a href="../../events/marvel/SecretWarsI.php" target="_self">Secret Wars</a>. When his familiar red and blue 
			costume had became torn and tattered from battle he sought to replicate a new costume. He heard from other 
			heroes of a machine that could replicate clothing. Not knowing exactly where the clothing replicator was, 
			Spider-Man mistakenly activated a machine that produced a little black ball which slithered over his body 
			when he touched it covering him from head to toe in a black costume.</p><br>
			
			<p>Returning to Earth, Spider-Man did not give much thought to the properties of his new costume, which 
			could imitate any clothing, "dimensionally" store small items and had seemingly an inexhaustible supply of 
			webbing. He became suspicious of its properties upon learning it was organic in nature. Taking an earlier 
			offer from Reed Richards (<a href="../../heroes/marvel/MrFantastic.php" target="_self">Mr. Fantastic</a>) 
			to study the costume, he discovered that it is a symbiotic organism that was attempting to fuse itself to 
			him both physically and mentally. Richards was able to use sonics to sever the bond and trap the symbiote 
			within an environmental containment cell.</p><br>
			
			<p>The symbiote would escape Richard's prison and reclaim its host. Disguising itself as the red and blue 
			Spider-Man costume, Peter Parker would not discover it was actually the symbiote until it was too late. He 
			was forced to battle the symbiote while fighting off the Vulturians, a group of convicts that had adopted  
			the technology of the <a href="../../villains/marvel/VultureII.php" target="_self">Vulture</a>, desiring to 
			prove their mettle by defeating Spider-Man. After defeating the Vulturians, Spider-Man resolved to be free 
			of the symbiote by gambling that the ringing bell of a local church tower would drive the symbiote away 
			before he would succumb to the ear shattering noise. The symbiote was driven away, but Parker became 
			unconscious and lay dying. The symbiote, which had been bound to Parker long enough to presumably assume 
			his sense of duty and responsibility, would seemingly sacrifice itself to save its host by dragging him down 
			the bell tower's stairs.</p><br>
			
			<p>Weak and dying, the symbiote would tune into the emotions of Eddie Brock, a reporter who blamed Spider-Man 
			for his failed marriage and career as a journalist. Brock's series of articles written on the confessions of 
			the murdering vigilante known as the Sin-Eater were exposed to be the ramblings of a compulsive confessor, 
			after Spider-Man captured the actual killer, police officer Stan Carter. Adding further misery to his personal 
			and professional failures Brock was dying of cancer and had gone to the church to beg forgiveness from God for 
			contemplating suicide. The symbiote detecting Brock's strong emotions about Spider-Man bound itself to the new 
			host becoming the monstrous <span class="bold">Venom</span>.</p><br>
			
			<p>Dying of cancer Eddie Brock decides to auction for charity to the highest bidder the 
			<a href="../../aliens_races/marvel/Klyntar.php" target="_self">alien symbiote</a> from which he derives 
			his powers as <a href="VenomI.php" target="_self">Venom</a>.</p><br>
			
			<p>In a last ditch effort to regain turf and criminal credibility New York mobster Don Fortunato purchases the symbiote 
			to be used by his son Angelo - who never lived up to his father's expectations. Consumed by his anger Angelo became even 
			more powerful than Brock, but ultimately his doubts drove the alien symbiote to abandon its new host leaving him to 
			fall to his death after a short and murderous career as <span class="bold">Venom</span>. Seeking a new host that shared 
			both its hatred of <a href="../../heroes/marvel/SpiderManI.php" target="_self">Spider-Man</a> as well as the will and 
			experience of a seasoned criminal the symbiote offered itself to Mac Gargan, then known as the 
			<a href="Scorpion.php" target="_self">Scorpion</a>, transforming him for a time into 
			<span class="bold">Venom</span>.</p><br>			
