		<td class="white_cell">
			<p>Dr. Scarlotti, the original <span class="bold">Vulture</span> was a criminal scientist who battled the android 
			<a href="../../heroes/marvel/HumanTorchI.php" target="_self">Human Torch</a> in the years following World War II.</p><br>
			
			<p>Years later an engineer and inventor named Adrian Toomes designed an electro-magnetic harness with a pair of wings 
			to commit crimes with the goal of further financing his inventions as the <span class="bold">Vulture</span>.</p><br>
			
			<p>After Toomes was defeated by <a href="../../heroes/marvel/SpiderManI.php" target="_self">Spider-Man</a> and 
			incarcerated he revealed the location of a second Vulture costume to his cellmate Black Drago. After escaping prison 
			Drago used Toomes' technology to engage in criminal activity. Drago was defeated by Spider-Man and 
			<a href="KravenI.php" target="_self">Kraven the Hunter</a> and sent back to prison. He teamed with his cellmate Toomes 
			to escape and seek revenge upon Spider-Man, but when free Toomes turned on Drago to humiliate him and reclaim the sole 
			mantel of the Vulture.</p><br>
			
			<p>Shortly after Toomes was re-incarcerated, Dr. Clifton Shallot, a professor at Empire State University acquired the 
			Vulture's wings from prison authorities. Using his expertise in biology he altered his appearance and granted himself 
			powers similar to the Vulture. Shallot was cured of his mutation and brought to justice by Spider-Man.</p><br>
			
			<p>After years of exposure to elements in his wing harness which allowed him to fly, Toomes contracted cancer. The 
			criminal scientist eventually was cured and physically rejuvenated becoming an even more dangerous menace.</p><br>
