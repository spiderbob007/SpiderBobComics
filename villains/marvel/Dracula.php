			<p>Prince Vlad Dracula was born in the early 15<sup>th</sup> Century as the second son of Transylvanian nobleman 
			Vlad Dracul. After Dracul was ousted from the throne of Wallachia by his half-brother Alexandru Aldea, he became 
			an ally of the Ottoman Turks.</p><br>
			
			<p>As a show of loyalty, Dracul offered his sons Vlad and Radu to a Turkish leader. While in the care of the Turks, 
			Radu died and Vlad fostered a hatred of humanity.</p><br>
			
			<p>In the mid-15<sup>th</sup> Century, Vlad Dracula escaped the Turks and claimed the throne of Wallachia. Dracula 
			was shortly thereafter forced into exile. He would wander from nation to nation until settling into marriage to a 
			woman he did not love a decade later. His daughter by this marriage would become the vampire known as Lilith.</p><br>
			
			<p>Soon after the end of his marriage, Dracula would reclaim the throne of Wallachia and wage a bloody war with the 
			Turks. His practice of impaling hordes of enemies gained him the name "Vlad the Impaler." During this time of war 
			he married a second time, siring a son named Vlad Tepelus.</p><br>
			
			<p>After years of battle with the Turks, Castle Dracula was captured by the warlord Turac. Learning that his wife 
			had been raped by Turkish soldiers, Dracula attacked Turac, who mortally wounded him. Turac took his enemy to be 
			healed by the gypsy Lianda, who was a vampire. She killed Dracula with her bite and he eventually rose as a 
			vampire.</p><br>
			
			<p>Dracula would later defeat the vampire Nimrod and succeed him as ruler of Earth's vampires. He would later 
			enhance his own strength with the blood of Varnae, the lone survivor of the first group of vampires to exist on 
			Earth, who were created by pre-Cataclysmic Atlantian sorcerers using the Darkhold, a primeval book of black 
			magic.</p><br>
			
			<p>Dracula would move throughout the world over the next six centuries facing defeat and resurrecting many times. 
			It was not until the sorcerer Dr. Strange used the incantation known as the Montesi Formula from the mystical book 
			known as the Darkhold that all vampires were destroyed including <span class="bold">Dracula</span>.</p><br>
