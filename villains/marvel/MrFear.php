			airship. Unknown to Saxon at the time of his murder of Drago, the incident was overheard by Larry Cranston, a 
			one-time law school classmate of Matt Murdock who suspected his identity as 
			<a href="../../heroes/marvel/Daredevil.php" target="_self">Daredevil</a>. Using his skills as a lawyer, Cranston 
			obtained a court order, under the guise of a Saxon' relative, to get custody of the Mr. Fear equipment. Cranston 
			plotted for years until he eventually struck at Murdock and Daredevil as Mr. Fear - a plot which also lead to his 
			death.</p><br>
			
