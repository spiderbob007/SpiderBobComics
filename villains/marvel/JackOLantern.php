			(<a href="Mysterio.php" target="_self">Mysterio I</a>) sought revenge against those she believed lead to her cousin's 
			suicide. For years Maguire Beck worked behind the scenes designing effects for her older cousin's flamboyant criminal 
			career. In those times when Quentin Beck was incarcerated he also had another protégé, Daniel Berkhart, who also used 
			the identity of <a href="Mysterio.php" target="_self">Mysterio</a> in the employee of 
