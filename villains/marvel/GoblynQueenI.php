			<p>The portion of Grey's personality co-opted by the Phoenix Force returned to Earth, but because of the Phoenix's 
			atrocities was rejected by the still sleeping Grey. The memories of the Phoenix would then key into Grey's clone 
			giving her consciousness.</p><br>
			
			<p>Mr. Sinister eventually used his clone, he named Madelyne Pryor, to initiate his plan to create a superhuman 
			offspring from the genetics of Jean Grey and Scott Summers 
			(<a href="../../heroes/marvel/Cyclops.php" target="_self">Cyclops</a>).</p><br>
			
			<p>Sinister sent Pryor to Alaska and placed her in a job as a pilot for North Star Airways, the air freight company 
			run by Summers' grandparents. When Summers first met Pryor he was immediately enamoured with her because of her 
			resemblance to his first love, Grey, who was still believed to be dead. The two were married and would eventually 
			have a child named Nathan Christopher Summers (<a href="../../heroes/marvel/Cable.php" target="_self">Cable</a>).</p><br>
				
			<p>Shortly after the infant was born, Cyclops and his fellow X-Man 
			<a href="../../heroes/marvel/Storm.php" target="_self">Storm</a> held a competition to determine which of them would 
			be leader of the X-Men. As yet, Pryor was unaware of her own latent superhuman abilities. However, she did not want 
			Scott to divide his time between her and the X-Men, so she unconsciously used her fledgling powers to influence the 
			contest. As a result, Cyclops lost, and he retired to live with Pryor and their son in Anchorage.</p><br>
			
			<p>When Grey's suspended and now healed body was found by the 
			<a href="../../teams/marvel/Avengers.php" target="_self">Avengers</a> - the strains in Summer's and Pryor's marriage 
			became to great and she left taking their son. Summers would readopt his identity as Cyclops leading the members of 
			the original X-Men in a team known as <a href="../../teams/marvel/XFactor.php" target="_self">X-Factor</a>.</p><br>
			
			<p>Learning that Grey was still alive, Mr. Sinister decided to eliminate Pryor, lest the truth of her origin be exposed. 
			Unleashing the team of assassins known as the Marauders, Pryor used her emerging powers to defend herself - but 
			Mr. Sinister abducted her infant son.</p><br>
			
