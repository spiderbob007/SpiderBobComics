	<tr>
		<td class="white_cell">
			<p>The <span class="bold">Crimson Dynamo</span> armor was created by Soviet scientist Dr. Anton Vanko, who was both 
			feared and despised by his nation's dictatorial leaders.</p><br>
			
			<p>Vanko was assigned to commit acts of sabotage against Tony Stark's industrial empire thereby crippling America's 
			military defenses. Challenging Stark's alter-ego, <a href="../../heroes/marvel/IronManI.php" target="_self">Iron Man</a>, 
			as the Crimson Dynamo, Vanko was defeated and persuaded by deception to defect.</p><br>
			
			<p>Hired by Stark as his chief electronics research engineer, Vanko was targeted for execution by his former Soviet 
			masters.</p><br>
			
			<p>Soviet spies, Natasha Romanov (<a href="../../heroes/marvel/BlackWidowII.php" target="_self">Black Widow II</a>) 
			and Boris Turgenov, were sent to America to assassinate Vanko, but instead Turgenov kidnapped the scientist and donned 
			the Crimson Dynamo armor to defeat Iron Man.</p><br>
			
			<p>Rescuing Vanko, Iron Man and the scientist setout to defeat the Soviet spies. In a last desperate attempt to stop 
			Turgenov, Vanko used an untested laser weapon which lead to the apparent death of both men. For her failure to destroy 
			Iron Man, the Black Widow was forced into exile to avoid execution.</p><br>
			
			<p>The next appearance of the Crimson Dynamo would be an invention of Cord Industries scientist Alex Niven, who had 
			changed has last name from Nevsky.</p><br>
			
			<p>Nevsky was an assistant of the first Crimson Dynamo, Dr. Vanko. When Vanko defected his friends and associates in 
			the Soviet Union were repressed. Nevsky fled to the United States and plotted his vengeance against Stark and 
			Iron Man.</p><br>
			
			<p>Nevsky would separately battle both Tony Stark and Eddie March, a professional boxer who had to quit the ring, but 
			stood in as Iron Man for Stark when he temporarily retired due to health concerns.</p><br>
			
			<p>Nevsky was also being pursued by the Titanium Man, and armored agent of the Soviet Union. The Soviets desired Nevsky's 
			genius to build an armor capable of destroying Iron Man.</p><br>
			
			<p>A battle between the three armored warriors resulted in the death of Janice Cord, the woman loved by both Stark and 
			Nevsky, when the Titanium Man used his High-Voltage Electric-Beam. Nevsky swore vengeance upon Iron Man for interfering 
			and causing Cord's death.</p><br>
			
			<p>Nevsky would eventually be executed as a traitor by the Soviet Union.</p><br>
			
			<p>The next Crimson Dynamo, Yuri Petrovitch, was assigned to apprehend his defector father and the Black Widow, who was 
			then serving as a member of the superhero team known as the 
			<a href="../../teams/marvel/Champions.php" target="_self">Champions</a>. Failing to complete his mission he was imprisoned 
			in Siberia.</p><br>
			
			<p>After Petrovitch was relieved of his armor, espionage agent Dimitri Bukharin became the Crimson Dynamo and was assigned 
			duty as a member of the Soviet Super-Soldiers to spy on the group. After his mission was discovered he was expelled from 
			the team.</p><br>
			
			<p>Bukharin served as a solo agent until a disastrous encounter in the United States with the former Soviet agent known as 
			the Titanium Man. After the collapse of the Soviet Union, Bukharin became the hero known as Airstrike.</p><br>
			
			<p>Bukharin's failure in the United States was engineered by the former Soviet politician, Valentin Shatalov, who desired 
			the armor for himself. After acquiring the armor, Shatalov formed a group dedicated to reclaiming the former glory of the 
			Soviet Union called Remont 4.</p><br>
			
			<p>The Crimson Dynamo would not been seen again until years after the collapse of the Soviet Union, when student Gennady 
			Gavrilov unearthed an armor's helmet stored away in a government building remotely reactivating a prototype armor.</p><br>
			
			<p>Over the years various others wore Crimson Dynamo armors acquired on the black market by criminals. In one incident 
			an early model was activated as part of a program developed by the Soviet Union in the event of a nuclear war, but the 
			armor seems to be currently back under the control of the Russian government and is piloted by a soldier working in 
			the interest of the Russian people as the <span class="bold">Crimson Dynamo</span>.</p><br>
