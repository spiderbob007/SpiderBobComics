			<p>Mercenary Jason Macendale began his career in the Marines and later the CIA, but defected to a foreign military 
			power, an act which resulted in his American citizenship being revoked.</p><br>
			
			<p>Wanting notoriety as a mercenary, he assumed the identity of the costumed criminal 
			<span class="bold">Jack O'Lantern</span> and attacked the android 
			<a href="../../heroes/marvel/X51.php" target="_self">Machine Man</a>. His exploits brought him to the attention of 
			Wilson Fisk's (<a href="Kingpin.php" target="_self">Kingpin</a>) right hand man the Arranger, who hired him as the 
			Kingpin's principle enforcer.</p><br>
			
			<p>Jack O'Lantern was partnered with another costumed criminal, <a href="HobgoblinII.php" target="_self">Hobgoblin</a>, 
			by the Arranger, and the Hobgoblin's partner the Rose to overthrow the Kingpin and control the crime families of New 
			York.</p><br>
			
			<p>Under fire from the police, Hobgoblin spotted his enemy 
			<a href="../../heroes/marvel/SpiderManI.php" target="_self">Spider-Man</a> and demanded Jack O'Lantern assist him in 
			killing the hero. Jack O'Lantern not liking the odds of an altercation with both the police and Spider-Man practiced 
			discretion and fled, an act of betrayal which prompted Hobgoblin to attempt an assassination against 
			Jack O'Lantern.</p><br>
			
			<p>Believing he knew Hobgoblin's true identity, 
			<a href="../../locations/marvel/DailyBugle.php" target="_self">Daily Bugle</a> reporter Ned Leeds, Jack O'Lantern 
			hired the assassin known only as the Foreigner to kill the reporter while on assignment in Berlin. The Foreigner 
			retrieved Leeds' Hobgoblin equipment and Macendale became the new <span class="bold">Hobgoblin</span>.</p><br>
			
			<p>In actuality Leeds was being controlled by the actual Hobgoblin, Roderick Kingsley, who had retired from taking 
			a direct hand in criminal activity.</p><br>
			
			<p>During an invasion of Earth by the forces of Limbo, known as Inferno, Macendale would attempt make a deal with the 
			demon N'astirh to sell his soul for demonic powers. The demon would refuse his soul but instead transformed him for 
			his amusement by having a demon possess his body, granting him super-human powers but twisting his visage into demonic 
			form, making him a <span class="bold">Demo-Goblin</span>.</p><br>
			
			<p>Macendale would later be free of the demon, but not one to learn his lesson, would return once again after 
			cybernetic enhancements as the <span class="bold">Cyber-Goblin</span>.</p><br>
