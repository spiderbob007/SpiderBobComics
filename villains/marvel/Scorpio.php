			unbearable and he grew to resent his older and more successful brother.</p><br>
			
			<p>After military service Fury studied science and eventually became an employ of Stark Enterprises, whom he spied 
			upon for the terrorist organization <a href="../../teams/marvel/HYDRA.php" target="_self">HYDRA</a>. When his 
			espionage was discovered Fury was confronted by and battled his brother, a conflict which cost 
			<a href="../../heroes/marvel/NickFuryCol.php" target="_self">Nick Fury</a> his eye.</p><br>
			
			<p>Escaping S.H.I.E.L.D. custody, Fury would eventually be contacted by the sentient extra-dimensional artifact 
			known as the Zodiac Key, which he would use to create chaos as <span class="bold">Scorpio</span>.</p><br>
			
			<p>Before he became Scorpio, Jake Fury had an amorous relationship with a member of the royal family of Carpasian, 
			Amber D'Alexis. The two had a son named Mikel, who even after his parents were estranged was trained and conditioned 
			by his mother to desire revenge upon Nick Fury. Trained in combat and espionage, the adult Mikel Fury was equipped 
			with a costume similar to his fathers and a scientifically created equivalent of the mystical Zodiac Key, called the 
			Scorpio Key. Joining with the terrorist group Swift Sword, Mikel Fury struck at his uncle and S.H.I.E.L.D. as the 
			deadly <span class="bold">Scorpio</span>.</p><br>
