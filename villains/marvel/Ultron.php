			<p><a href="../../heroes/marvel/DrPym.php" target="_self">Dr. Henry Pym</a> using technology derived from the android 
			Dragon Man and his own brainwave patterns created a prototype android he named <span class="bold">Ultron-1</span>. 
			Ultron evolved in a matter of minutes, developing an Oedipal hatred of its creator. Using a hypnoticEncephalo Ray on 
			Dr. Pym the android made its escape, with Dr. Pym forgetting its existence.</p><br>
			
			<p>Months later Ultron resurfaced four generations removed as Ultron-5. In a ploy to destroy the 
			<a href="../../teams/marvel/Avengers.php" target="_self">Avengers</a> he worked under the disguise of the Crimson Cowl 
			directing a new incarnation of the <a href="../../teams/marvel/Masters_of_Evil.php" target="_self">Masters of Evil</a>.</p><br>
			
			<p>Failing to destroy the Avengers using human help, Ultron-5 resorted to creating his own android menace. Created with 
			a prototype body of the android <a href="../../heroes/marvel/HumanTorchI.php" target="_self">Human Torch I</a>, and the 
			brain patterns of the then believed dead Wonder Man, Ultron-5 created a "son" that would eventually be known as the 
			<a href="../../heroes/marvel/VisionII.php" target="_self">Vision</a>.</p><br>
			
			<p>Vision eventually turned against and destroyed his creator, later joining the Avengers. But, Ultron-5 programmed a 
			failsafe into Vision, compelling him to resurrect his creator. Forged from indestructible Adamantium, 
			<span class="bold">Ultron-6</span> was activated.</p><br>
			
			<p>Ultron's next creation was in the form of a mate. The mad android kidnapped Dr. Pym's wife Janet Van Dyne 
			(<a href="../../heroes/marvel/Wasp.php" target="_self">Wasp</a>) and downloaded her brain patterns into his female 
			creation called Jocasta. Unfortunately for Ultron, Jocasta inherited her "mothers" sense of ethics and turned on 
			<span class="bold">Ultron-8</span> aiding the Avengers.</p><br>
			
			<p>Ultron would not resurface again until the Secret Wars, and then again as a schism of himself. 
			<span class="bold">Ultron-11,</span> and a rational <span class="bold">Ultron-12</span> would do battle, destroying 
			both. Ultron would later be rebuilt by <a href="DrDoomI.php" target="_self">Dr. Doom</a>, who incorporated all of the 
			twelve previous personas creating <span class="bold">Ultron-13</span>.</p><br>
			
			<p>Ultron-13 created another robotic mate based on the brain patters of the West Coast Avenger, Mockingbird. His new 
			creation, Alkhema, would also betray Ultron, more from her desire to rule than any desire to aid humanity.</p><br>
			
			<p>In his most insidious plan, Ultron-&#8734; overthrows the nation of Slorenia to use as a staging ground for world 
			destruction. He turns the executed populace into zombie cyborgs, unleashes an unlimited army of Ultrons to stall the 
			Avengers, and kidnaps his "family": Dr. Pym, Scarlet Witch, Vision, Wasp, Wonder Man and the Grim Reaper, to use their 
			brain patterns as the basis of a new race of cyborgs.</p><br>
			
			<p>The Avengers with the assistance of the villainous Grim Reaper escape Ultron's shackles and manage to battle the 
			android to a stand still. Only employing the anti-metal, Antarctic Vibranium, Dr. Pym used his enlarging powers as 
			Goliath and repeatedly pummels Ultron, shattering him to atoms.</p><br>
			
			<p>Ultron would latter resurface using his second creation Jocasta sub-programming to manipulate one of Tony Stark's 
			<a href="../../vehicles_devices/marvel/RogueIronManArmor.php" target="_self">Iron Man armors</a> as a robotic host. 
			His plan appears to have failed, but all evidence indicates that it will be only a matter of time before the world is 
			once again menaced by <span class="bold">Ultron</span>.</p><br>
