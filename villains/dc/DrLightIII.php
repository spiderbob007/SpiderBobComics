			secret information Light was confronted by Finlay in the disguise of Dr. Light - a heroic identity her created 
			using his light-based scientific inventions.</p><br>
			
			<p>Deducing that Finlay was the heroic Dr. Light, Light confronted his friend - which resulted in an accidental 
