	<tr>
		<td class="white_cell">
			<p>Matt Hagen was searching for sunken treasure when he discovered an underground grotto that contained a 
			bizarre glowing pool of protoplasm. Accidentally slipping into the pool, Hagen emerged with his skin transformed 
			into a clay-like consistency. Desiring to return into his formerly handsome form he found that he could mentally 
			control the shape of his body. Using his new found ability he engaged in a life of crime as 
			<span class="bold">Clayface</span>.</p><br>
			
			<p>Hagen discovered that his abilities were limited by a 48 hour time period and he returned time and again to 
			bath in the protoplasm. Hagen even developed a synthetic form of the protoplasm, but it began to have deleterious 
			effect on his body.</p><br>
			
			<p>While in prison after being apprehended yet again by the 
			<a href="../../heroes/dc/BatmanI.php" target="_self">Batman</a>, Hagen was visited by S.T.A.R. Labs scientist 
			Preston Payne, who desired to use the protoplasm to make himself handsome. Isolating an enzyme in Hagen's blood, 
			Payne injected himself with a serum which made his body malleable. Transforming himself into an Adonis, Payne 
			became quite the ladies man - unfortunately the serum manifested into a nasty side effect which liquefied his 
			body and made his touch reduce others to protoplasm.</p><br>
			
			<p>Realizing that his condition was contagious, Payne isolated himself within a powerful exoskeleton. To his 
			additional horror Payne discovered that his survival periodically demanded that he unleash his destructive power 
			upon hapless victims as the monstrous <a href="ClayfaceIII.php" target="_self">Clayface</a>.</p><br>
			
			<p>Years later in events unrelated to Hagen and Payne, Sondra Fuller had herself biologically altered by the 
			terrorist group Kobra. She used her powers to control the shape of her body to attack the 
			<a href="../../teams/dc/OutsidersI.php" target="_self">Outsiders</a> as <span class="bold">Lady Clay</span>.</p><br>
			
			<p>Eventually she joined with Payne and the original <a href="ClayfaceI.php" target="_self">Clayface</a>, 
			Basil Karlo, to exact revenge upon Batman as the Mud Pack. Failing in their revenge Fuller and Payne retreated 
			from humanity and bore an offspring which has been dubbed the <span class="bold">Claything</span>.</p><br>
