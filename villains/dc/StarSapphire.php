		<td class="white_cell">
			<p>Judged unfit to rule, the first <span class="bold">Star Sapphire</span> was deposed and exiled to the 
			7<sup>th</sup> Dimension by the Zamarons. From her other-dimensional prison she waged war against Earth, plotting to 
			make it her new base of operations. Her plans for planetary conquest were foiled by the first 
			<a href="../../heroes/dc/FlashI.php" target="_self">Flash</a>. In subsequent attacks she was foiled by the second 
			<a href="../../heroes/dc/FlashII.php" target="_self">Flash</a> and 
			<a href="../../heroes/dc/GreenLanternII.php" target="_self">Green Lantern</a> - her defeat resulting in imprisonment 
			within the Star Sapphire Gem.</p><br>
			
			<p>Chosen by the Zamarons to be their ruler <a href="../../cast/dc/CarolFerris.php" target="_self">Carol Ferris</a> 
			was granted the power of the mystical alien gem and for a time became <span class="bold">Star Sapphire</span>.</p><br>
			
			<p>Ferris would latter battle Dela Pharon - a rival for the honor of ruling the Zamarons. After Pharon's defeat her 
			whereabouts became unknown.</p><br>
			
			<p>In her role as Star Sapphire, Ferris faced numerous defeats at the hands of the 
			<a href="../../heroes/dc/GreenLanternII.php" target="_self">Green Lantern</a>, making her mentally unstable and 
			splitting her into two distinct personalities - one being the masculine criminal known as the Predator.</p><br>
			
			<p>On the planet Pandian, Remoni-Norta was offered the leadership of the Zamarons, but although she refused, she was 
			bequeathed one of the five Star Sapphire gems. Aware of the existence of four other gems Remoni-Norta came to Earth 
			in search of Carol Ferris so that she could challenge her for the power of 
			<span class="bold">Star Sapphire</span>.</p><br> 
