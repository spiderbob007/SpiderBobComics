		<td class="white_cell">			
			<p>A being created by Professor Zagarian of a synthetic human tissue called "plastalloy," the 
			<span class="bold">Shaggy Man</span> was intended to be an experiment to provide artificial body parts for 
			human patients.</p><br>
			
			<p>During the creation process the creature was subjected to an overdose of electric current causing it become 
			aware, instinctively attacking anything that moved.</p><br>
			
			<p>Members of the <a href="../../teams/dc/JLA.php" target="_self">Justice League</a> attempted to stop the 
			indestructible creature's rampage, but discovered that it was impossible. The final resolution was to create 
			another Shaggy Man and pit the two creatures against one another, sealing them in a deep pit to battle for an 
			eternity.</p><br>
			
			<p>The Shaggy Men would eventually escape to menace members of the Justice League. One would be destroyed during 
			the <a href="../../events/dc/CrisisOnInfiniteEarths.php" target="_self">Crisis on Infinite Earths</a>, when the 
			apparently weakened creature was destroyed by an explosive arrow fired by the archer 
			<a href="../../heroes/dc/ArsenalIII.php" target="_self">Speedy</a>. The other had been imprisoned in a plasma prison 
			by <a href="../../heroes/dc/GreenLanternII.php" target="_self">Green Lantern II</a> and sent to the bottom of the 
			ocean by <a href="../../heroes/dc/Aquaman.php" target="_self">Aquaman</a>, where it would be retrieved by General 
			Eiling.</p><br>
			
			<p>A career military man of poor scruples, <span class="bold">General Wade Eiling</span> was not above manipulating 
			others to further his goals.</p><br>
			
			<p>As an Air Force Major, Eiling was associated with the Atom Project. Seeking test subjects, he framed Captain 
			Nathaniel Adam for treason, offering amnesty if he volunteered for the project.</p><br>
			
			<p>The test appeared to have failed when Adam, encased in a nearly indestructible alloy, was apparently 
			disintegrated by a nuclear bomb. In truth Adam survived the blast being hurled decades into the future, where 
			General Eiling would continue to manipulate him as 
			<a href="../../heroes/dc/CaptainAtom.php" target="_self">Captain Atom</a>.</p><br>
			
			<p>Eiling's greatest ploy for world domination involved the creation of a team of military metahuman operatives 
			known as the <a href="../../teams/dc/UltramarineCorps.php" target="_self">Ultramarines</a>. Eiling convinced his 
			operatives that the <a href="../../teams/dc/JLA.php" target="_self">Justice League</a> were traitors and sent 
			them on a mission to kill the heroes.</p><br>
			
			<p>While the Ultramarines were battling the Justice League, Eiling was completing his plans to cheat death from 
			cancer by transferring his conscious into the indestructible body of the synthetic Shaggy Man, becoming the 
			unstoppable <span class="bold">General</span>.</p><br>			
