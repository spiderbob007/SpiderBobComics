		<td class="white_cell">
			<p><span class="bold">Pre-Crisis</span></p>
			<p>A wealthy young socialite Priscilla Rich suffered from an overwhelming inferiority complex. Organizing a charity 
			benefit Rich became insanely jealous when as the host she lost attention to
			<a href="../../heroes/dc/WonderWomanI.php" target="_self">Wonder Woman</a>. After a surreptitious attempt to murder 
			Wonder Woman, Rich returned home to find her evil persona confronting her in a mirror. Rich's alter ego explained 
			that she was her true self - a treacherous, relentless huntress. Crafting a costume in the form of a predatory cat 
			she became the <span class="bold">Cheetah</span>.</p><br>
			
			<p>The niece of Priscilla Rich, Debbi Domaine had feelings of guilt about her wealth and privilege which lead her 
			to join extreme ecological groups. Summoned to her aunt's deathbed Rich died before she could reveal her criminal 
			secret to her niece. Recoiled by the sight of her dead aunt Domaine staggered and was knocked unconscious.</p><br>
			
			<p>Domaine awoke to find herself in the hidden headquarters of the cult leader Kobra. He explained his intent to 
			recruit the Cheetah, but now that she was dead he would have to make a re-creation. Exposing Domaine to mental 
			conditioning and shock therapy Kobra created the hopelessly insane <span class="bold">Cheetah</span>.</p><br><br>
			
			<p><span class="bold">Post-Crisis</span></p>
			<p>Renowned archaeologist and treasure hunter Barbara Minerva sought proof of the existence of a tribe of feline 
			humanoids in South America. In her search she came upon the temple of Urzkartaga and a strange and rare blood-drinking 
			plant tied to the legend of the cat-people. Added by a local mystic Minerva used the herb to perform a ceremony that 
			transformed her into a ferocious <span class="bold">Cheetah</span>.</p><br>
			
			<p>Eventually a new heir to the power of Urzkartaga would arise, Sebastian Ballesteros, an agent of the 
			<a href="../../aliens_races/dc/Amazons.php" target="_self">Amazons'</a> old enemy 
			<a href="Circe.php" target="_self">Circe</a>. Feeding the mystic plant a blood sacrifice and partaking in the herbal 
			potion he would become an even more powerful <span class="bold">Cheetah</span>.</p><br>
