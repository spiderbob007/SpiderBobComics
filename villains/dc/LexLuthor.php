			<p><span class="bold">Pre-Crisis Earth-1</span></p>
			
			<p>A young <span class="bold">Lex Luthor</span> and his family had not lived long in 
			<a href="../../locations/dc/Smallville.php" target="_self">Smallville</a> when the opportunity arose for him to 
			save <a href="../../heroes/dc/SuperboyI.php" target="_self">Superboy</a> from a chunk of deadly 
			<a href="../../vehicles_devices/dc/Kryptonite.php" target="_self">Kryptonite</a>. The two became great friends 
			and Superboy built a state-of-the-art laboratory for Luthor, who set about the task of finding a cure for 
			Kryptonite poisoning.</p><br>
			
			<p>Creating a protoplasmic lifeform that held the key to the cure - Luthor's excitement lead to him knocking 
			over chemicals which sparked a fire. Seeing the lab fire Superboy used his super-breath to extinguish the 
			flames, but in the process destroyed the protoplasm and the resultant fumes caused Luthor's hair to fall out. 
			Accusing Superboy of being jealous of his genius Luthor withheld the cure for Kryptonite poisoning and vowed 
			revenge.</p><br><br>
			
			<p><span class="bold">Post-Crisis</span></p>
			
			<p>The son of working class parents, <span class="bold">Lex Luthor</span> decided to not follow in their 
			impoverished footsteps. At an early age he made considerable contacts with members of organized crime, but 
			never took a direct hand in anything illegal himself. As a teenager he arranged for his parents to have an 
			automobile accident for which he would be the sole recipient of a sizable insurance settlement.</p><br>
			
			<p>Using his small fortune he attended the Massachusetts Institute of Technology (MIT). An aggressive student 
			he graduated in record time and turned his genius toward patenting new inventions, which made him quite 
			wealthy.</p><br>
			
			<p>The de facto mayor of <a href="../../locations/dc/Metropolis.php" target="_self">Metropolis</a>, he was 
			considered by many to be the most powerful man in the city, if not the world, until the arrival of 
			<a href="../../heroes/dc/Superman.php" target="_self">Superman</a>.</p><br>
			
			<p>Testing the Man of Steel's abilities, Luthor arranged a terrorist attack at a party upon his yacht. 
