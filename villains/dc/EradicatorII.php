			This Eradicator returned Superman to the Fortress of Solitude to revive him within an energy matrix. Resurrected, 
			Superman joined with the Eradicator and other heroes to battle the Cyborg Superman. The Eradicator sacrificed its 
			energies to protect Superman from <a href="../../vehicles_devices/dc/Kryptonite.php" target="_self">Kryptonite</a> 
			energy released by the Cyborg Superman.</p><br>
			
			<p>The inert body of the Eradicator was taken to S.T.A.R. Labs where experiments by Dr. David Connor caused his 
