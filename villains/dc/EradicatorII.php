			thus he buried it in the Arctic. Within its icy grave the relic began construction upon a Kryptonian fortress 
			that would become the Fortress of Solitude. The fortress was designed to transform Earth and force Superman to 
			return to his Kryptonian heritage. Ultimately the plan failed and Superman cast the relic into the sun nearly 
			destroying the Eradicator program.</p><br>
			
			<p>The Eradicator returned once again with a humanoid form, but was defeated by Superman once again within the 
			Fortress of Solitude. After Superman's death at the hands of <a href="Doomsday.php" target="_self">Doomsday</a>, 
