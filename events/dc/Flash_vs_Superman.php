				but at the cost of spell being transferred to the swiftest. 
				Borrowing the speed from Superman, Garrick is able to catch his protégé, 
				but Abra-Kadabra uses the resulting energy to escape to the future 
				brining the heroes with him to use as a means to return to their time. 
				At the moment Garrick touches West and the spell is transferred, 
				Superman is able to see the "spell" as nothing more than parasitic 
				nanites. West uses his ability to manipulate molecules to make 
				Garrick intangible while Superman uses his heat vision to destroy the 
				microscopic robots. Meanwhile Abra Kadabra restocks his arsenal of 
				"magic" tricks from the future, but is defeated by the trio of heroes 
				and returned to his prison cell in the 21st Century.</p><br>		
