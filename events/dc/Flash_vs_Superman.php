				<p>Investigating a UFO
				<a href="../../heroes/dc/Superman.php" target="_self">Superman</a> and the
				<a href="../../heroes/dc/FlashII.php" target="_self">Flash</a> 
				are captured by two alien races who have been at war for millions of years. The two 
				races, which were once one race, had been at war for so long they had 
				forgotten what started the acrimony. Deciding it was time to end 
				the war one race sent a soldier forward in time making use of time's 
				circular nature - thereby arriving before the beginning of the war. 
				Threatening to destroy the Earth the Flash was forced to travel ahead in 
				time to insure the soldier's mission was successful. The competing 
				alien race sent Superman to foil the Flash because if time were to 
				change then their organic space travel technology would not have seeded
				<a href="../../locations/dc/Krypton.php" target="_self">Krypton</a> 
				with life and thus Superman would not exist. The two heroes race to the end of 
				time dodging obstacles and eventually defeating the aliens.</p><BR>
			
