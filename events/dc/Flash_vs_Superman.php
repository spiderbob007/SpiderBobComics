				the Man of Steel was more than willing - although the fifth-dimensional 
				imp had lied as was secretly rooting for the Flash, confident that his 
				foe Superman could beat the Scarlet Speedster. Although the Fasted 
				Man Alive wanted nothing to do with the race Superman used reverse 
				psychology to goad the Flash into agreeing to the race - which was run 
				on a obstacle course racetrack devised by Mxyzptlk. Stretched to 
				the ends of their endurance the two heroes finished the race with the 
				Flash barely winning and Mxyzptlk banished by his own deception.</p><br>
			
