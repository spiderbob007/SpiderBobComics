			began the systematic destruction of the Multiverse. Pariah's breach into the
			anti-matter universe also awoke the Monitor, who gathered a battalion of
			select heroes and villains from multiple dimensions in a hopes of stemming the 
			Anti-Monitor's assault and save the the positive-matter universes.</p><br>
		
