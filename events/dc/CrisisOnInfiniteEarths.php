 	</tr>
 	<tr>
  		<td class="white_cell">
			<p>The Crisis on Infinite Earths was both an economic and artistic decision designed to
			breath new life into the characters of the DC universe as well as relieve
			the creative and editorial staff of a burdensome and conflicting
			continuity accumulated over 45 years.</p><br>

			<p>The title <em>Crisis On Infinite Earths</em> came from a story arc by writer/editor Garner Fox in 
			<a href="../../issues/dc/CrisisonEarth1.php" target="_self">Justice League of America Vol. 1 #21</a> (Aug. 1962) 
			and 
			<a href="../../issues/dc/CrisisonEarth2.php" target="_self">Justice League of America Vol. 1 #22</a> (Sept. 1962), 
			titled <em>Crisis on Earth-One!</em> and <em>Crisis on Earth-Two!</em>, in which the 
			<a href="../../teams/dc/JLA.php" target="_self">Justice League of America</a> 
			on Earth-1 teamed with the 
			<a href="../../teams/dc/JSA.php" target="_self">Justice Society of America</a> 
			on Earth-2 to stop a group of villains from both
			Earths which had plans to conquer the planets. This story began the concept of a multiverse.</p><br>
			
			<p>To understand the DC Universe, readers must think of	history in aspects of Pre-Crisis and 
			Post-Crisis.</p><br>
			
			<span class="left">
			<p>Examples of alterations in continuity created by the Crisis on Infinite Earths are:</p><br>
			<p><span class="bold">1.</span> Pre-Crisis Princess Diana, the 
			<a href="../../heroes/dc/WonderWomanI.php" target="_self">Wonder Woman</a> 
			from World War II and a member of the Justice Society of America, became 
			<a href="../../heroes/dc/WonderWomanIII.php" target="_self">Hippolyta</a> 
			the mother of the current Wonder Woman, in effect making Princess Diana Wonder
			Woman II, as opposed to Wonder Woman I.</p><br>
			
			<p><span class="bold">2.</span>In the Post-Crisis world there never existed either a 
			Superbaby or Superboy. Kent's powers as 
			<a href="../../heroes/dc/Superman.php" target="_self">Superman</a>
			did not develop until puberty.</p><br>
			
			<p><span class="bold">3.</span>In the Post-Crisis world Selina Kyle 
			(<a href="../../villains/dc/CatwomanI.php" target="_self">Catwoman</a>)
			never married Bruce Wayne (<a href="../../heroes/dc/BatmanI.php" target="_self">Batman</a>)
			having a daughter who would become Huntress II, nor did Dick Grayson continue his career as 
			<a href="../../heroes/dc/RobinI.php" target="_self">Robin</a> into adulthood.</p><br>
			</span>
			
			<p>The apparent benefits of the Crisis is for the creative
			staff of DC Comics and die-hard fans only, because due to the nature of
			characters in periodic publications, only the story arcs being published
			by the current creators of the past few years generally have relevance to
			most readers. A good analogy of this phenomenon would be the <em>James Bond 007</em>
			films. All the characters change, except for Bond, who is obviously
			played by different actors, yet everyone accepts him as the same guy.</p><br>
		</td>
