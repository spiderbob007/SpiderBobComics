			similar to <a href="../../heroes/marvel/IronManI.php" target="_self">Iron Man</a>'s gauntlets, which would allow Spider-Man to 
			mentally control the type of webbing he created.</p><br>
				
			<p>Marvel Editor in Chief, Jim Shooter, liked the idea of the black costume and offered Schueller $220 for his idea and an opportunity to script 
			the story. Although Schueller's script ideas were never used by Marvel, his idea later appeared as part of the limited series 
			<a href="../../events/marvel/SecretWarsI.php" target="_self"><i>Marvel Super-Heroes Secret Wars</i></a>.</p><br>

			<p>Before the publication of <i>Amazing Spider-Man</i> #252 comics' fans had already expressed a dislike of the black Spider-Man costume and the 
			creative team of <i>Amazing Spider-Man</i> were directed by to get rid of the costume. Because the first appearance of the black costume was to 
			appear in issue eight of <i>Marvel Super-Heroes Secret Wars</i>, seven months after its first appearance, the creative team had to create a reason 
			to keep the new costume around. As a plot device, Tom DeFalco and Ron Frenz made the costume an alien symbiote that was attempting to bond with 
			Spider-Man as its host. After the symbiote was removed using sonic waves, Spider-Man returned to his classic costume, but over time the fans 
			grew to like the black costume, which lead to the storyline that resulted in the creation of 
			<a href="../../villains/marvel/VenomI.php" target="_self">Venom</a>.</p><br>
