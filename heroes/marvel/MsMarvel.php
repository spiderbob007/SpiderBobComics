		<td class="white_cell">
			<p><span class="bold">Carol Danvers</span> excelled in her United States Air Force career and eventually became 
			a trusted espionage agent. After retiring from a career of espionage she became the security chief at NASA's 
			Cape Canaveral facilities. It was during this time she met the Dr. Walter Lawson, who was secretly the Kree spy 
			<a href="CaptainMarvelI.php" target="_self">Captain Mar-Vell</a>.</p><br>
			
			<p>It was during a battle between Captain Marvel and his superior officer and rival Yon-Rogg, that Danvers was 
			exposed to an unknown radiation by the Kree Psyche-Magnitron. The extraterrestrial radiation merged Danvers' 
			biology with Captain Marvels making her a human/Kree hybrid although apparently not affecting Mar-Vell. 
			Eventually her altered biology granted her superhuman abilities, which she used to fight against injustice as 
			<span class="bold">Ms. Marvel</span>.</p><br>
			
			<p>Eventually Danvers was betrayed by the mutant terrorist known as 
			<a href="../../villains/marvel/Mystique.php" target="_self">Mystique</a>, who had her protégé 
			<a href="Rogue.php" target="_self">Rogue</a> use her abilities to rob Ms. Marvel of her powers. In addition to 
			draining her powers, Rogue's ability also robbed Danvers of her memories. With the intervention of 
			<a href="ProfessorX.php" target="_self">Professor Xavier</a>, she was able to regain her memories, but not the 
			emotional bonds linked to those memories.</p><br>
			
			<p>During an adventure in space with the <a href="../../teams/marvel/XMen.php" target="_self">X-Men</a>, Danvers 
			was subjected to involuntary testing by the alien insects known as the 
			<a href="../../aliens_races/marvel/Brood.php" target="_self">Brood</a>. The test had the side effect of 
			reactivating and boosting Danvers abilities to levels comparable to the 
			<a href="SilverSurfer.php" target="_self">Silver Surfer</a>. Danvers decided to remain in space with a group 
			known as the <a href="../../teams/marvel/Starjammers.php" target="_self">Starjammers</a>, assuming the name 
			<a href="Binary.php" target="_self"><span class="bold">Binary</span></a>.</p><br>
			
			<p>Eventually Danvers would return to Earth in an attempt to reconcile her relationships with friends, family 
			and teammates. She would rejoin the ranks of the Avengers, assuming an identity that paid homage to her career 
			as a military aviator, for a time calling herself <span class="bold">Warbird</span>.</p><br>

			<p>With the encouragement of fellow <a href="../../teams/marvel/Avengers.php" target="_self">Avengers</a>, 
			<a href="CaptainAmericaI.php" target="_self">Captain America</a>, Danvers would fully assume the legacy 
			and the title of <span class="bold">Captain Marvel</span>.</p><br>
