		<td class="white_cell">
			<p>The son of nuclear researcher Brian Xavier and his wife, Sharon, Charles' telepathic powers began to emerge 
			when he was still a boy. Following her husband's accidental death, Sharon married Brian's colleague, Dr. Kurt Marko. 
			Cain, Kurt's son by a previous marriage, came to live at the Xaviers' Westchester mansion shortly thereafter. A cruel 
			and spiteful boy, Cain bullied his new stepbrother. As punishment, his father secretly beat him, exposing the young 
			mutant's untrained mind to the experience of his sibling's pain firsthand. As he grew older, Xavier learned to control 
			his burgeoning powers.Enrolling in the graduate-studies program at Oxford University, Xavier met and fell in love with 
			a young Scotswoman named Moira Kinross. Of all their classmates, Charles and Moira were the only ones who understood 
			the ultimate implications of genetic mutation. Their passionate discussions on the subject gave way to an equally 
			passionate romantic relationship, and the two planned to be married. Standing in their way was Moira's former boyfriend, 
			Joe MacTaggert, a lance corporal in the Royal Marines -- a bully and thug, just like Cain. In MacTaggert's eyes, Xavier 
			was an effete, good-for-nothing intellectual. To prove him wrong and validate himself to his rival in terms that he would 
			understand, Xavier enlisted in the military after completing his studies at Oxford.</p><br>
			
			<p>His first day in combat, he thought he would go mad. Nothing could have prepared him for the mental horrors he would 
			experience as his fellow soldiers were maimed and killed around him. Somehow finding a way to cope, Xavier became something 
			of a legend in the area of search and rescue, being able to secretly use his power to pinpoint lost soldiers.</p><br>
			
			<p>Crushed when Moira broke off their engagement without explanation, Xavier traveled abroad after leaving the Army. While 
			in Cairo, Egypt, he clashed with Amahl Farouk (Shadow King), a mutant capable of summoning forth the darkness in the souls 
			of others. This confrontation led to Xavier's decision to devote his life to protecting humanity from evil mutants bent on 
			world domination and safeguarding innocent mutants from human oppression.</p><p>Traveling to Israel, Xavier fell in love 
			with an Israeli diplomat named Gabrielle Haller. Also, he befriended fellow drifter Erik Magnus Lehnsherr, the mutant who 
			would become his greatest enemy and rival; the genetic terrorist known as 
			<a href="../../villains/marvel/Magneto.php" target="_self">Magneto</a>, Master of Magnetism. Xavier espoused his optimistic 
			belief that Homo sapiens and Homo superior could coexist, but Lehnsherr foresaw mutants as the new minority to be persecuted 
			and hunted because of their differences.</p><br>
			
			<p>Leaving Israel for London Xavier was unaware that Haller was pregnant with his son. Only years later did he learn of 
			his connection to the psionically powered mutant known as Legion. While in London, he renewed his friendship with Moira, 
			who had became a renowned geneticist after marrying Joe MacTaggert. The two began to discuss the idea of founding a school 
			for mutants at his estate in New York state. En route to the United States, Xavier encountered an alien scout calling 
			himself Lucifer. The altercation with the alien left Xavier's legs crippled.</p><br>
			
			<p>Returning to his ancestral home Xavier began to establish his institute for gifted children. His first student was a 
			young Jean Grey, who had been traumatized by the dying thought of her best friend. After his success with Grey he began 
			to locate young mutants, enrolling them in his school. The first of his students: 
			<a href="Cyclops.php" target="_self">Cyclops</a>, <a href="AngelII.php" target="_self">Angel</a>, 
			<a href="Beast.php" target="_self">Beast</a> & <a href="Iceman.php" target="_self">Iceman</a> would join his first 
			student <a href="PhoenixII.php" target="_self">Marvel Girl</a> as the X-Men. In time his charges would be supplemented 
			by new groups of students to fulfill the dream of mutants and humans living together in peace and harmony, the dream of 
			<span class="bold">Professor X</span>.</p><br>
