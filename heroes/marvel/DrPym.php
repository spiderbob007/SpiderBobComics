	<tr>
		<td class="white_cell">
			<p><span class="bold">Dr. Henry Pym</span> was driven to battle injustice and inhumanity by the murder of his 
			first wife -- Maria Trovaya, a political dissident from Hungary. His scientific genius and knack for invention 
			made possible such pursuits. Naively believing Hank's American citizenship would protect her, Maria traveled to 
			her homeland with her husband shortly after their wedding. Confronted by the fascistic secret police, Dr. Pym 
			was knocked unconscious and his wife murdered.</p><br>
			
			<p>Returned to the United States, a distraught Pym discovered a rare group of subatomic particles he was able to 
			entrap in two separate serums through the application of magnetic fields. One formula would reduce the size of 
			people and objects; the other would restore them to normal height. Testing the serum on himself, he discovered 
			it was far more powerful than he had hypothesized. Reduced to the size of an insect, Pym found himself trapped 
			in an anthill and pursued by its inhabitants. Following his escape, he restored himself to normal. However, his 
			brush with death led him to conclude the formulas were too dangerous to exist.</p><br>
			
			<p>Weeks after Pym destroyed his creations, he reconsidered his rash decision and began to recreate the serums 
			in secret. Inspired by his astonishing experience, he undertook a study of ants. Culminating months of work, Pym 
			devised a cybernetic helmet allowing him to communicate with organized insects via psionic electrical waves.</p><br>
			
			<p>His innovations were put into practice when enemy intelligence operatives, interested in another of his projects 
			held him hostage. As the agents searched his lab, Pym stole away and donned his cybernetic helmet and costume. 
			Shrinking to the size of an insect, Pym entered an anthill commanding the swarm to attack his captors and free 
			his assistants. Successful Pym was inspired to begin a crime fighting career as <span class="bold">Ant-Man.</p><br>
			
			<p>When an alien entity from Kosmos murdered a fellow colleague, Dr. Vernon Van Dyne, Pym revealed his heroic 
			identity to the scientist's distraught daughter, Janet, who had a striking resemblance to his murdered wife. To 
			avenge her father's death, Pym genetically altered Van Dyne, granting the ability to sprout insect-like wings when 
			she reduced size using his Pym Particles, becoming the <a href="Wasp.php" target="_self">Wasp</a>. Helping Ant-Man 
			track down and defeat her father's killer the two became partners.</p><br>
			
			<p>Eventually the diminutive heroes' crime fighting partnership resulted in a much deeper relationship, and they 
			eventually fell in love. Shortly after the duo helped found the 
			<a href="../../teams/marvel/Avengers.php" target="_self">Avengers</a>, Pym developed a new serum that enabled him 
			to increase in size and mass becoming <span class="bold">Giant-Man.</p><br>
			
			<p>Eventually the two would retire when the dangers of their chosen profession proved too great and Pym began to 
			suffer health problems due to his increase in mass as Giant-Man. Remaining a scientist for the team he pursued 
			technological excellence, a pursuit that resulted in his greatest failing, the robot 
			<a href="../../villains/marvel/Ultron.php" target="_self">Ultron</a>. The living machine inadvertently gained 
			awareness and emotions. Believing itself as the next step in evolution the robot turned against its creator, 
			becoming one of the Avengers' greatest adversaries.</p><br>
			
			<p>Long in love with Van Dyne, Pym repressed a second personality forcing him to resist marriage. When a freak 
			laboratory accident triggered a radical psychological shift, he adopted a new identity 
			<span class="bold">Yellowjacket</span>.</p><br>
			
			<p>In a state of psychological distress he claimed to have "murdered" Henry Pym, and in the guise of Yellowjacket 
			kidnapped Van Dyne and proposed marriage. Aware it was Pym, she played along so as not to exacerbate his condition. 
			The couple were married at Avengers Mansion, which was attacked immediately thereafter by the Circus of Crime. 
			Shocked at seeing Van Dyne in danger Pym was stunned back to his normal personality, and he helped the Avengers 
			defeat the big-top terrorists. Despite the strange circumstances of their union, the two agreed to stay together. 
			Their rocky marriage ultimately ended in divorce, largely due to Pym's emotional instability and guilt over the 
			creation of Ultron. However, the two are romantically linked once again.</p><br>
			
			<p>Ultimately, Pym discovered that both he and Van Dyne no longer required the Pym Particles to activate their 
			powers. Developing a microscopic arsenal of weaponry and other devices he reunited with his true love in the Avengers 
			as <span class="bold">Goliath</span>.</p><br>
