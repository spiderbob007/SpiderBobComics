			the attention of an entertainment agent. After completing a television performance in his masked identity Parker failed 
			to stop a fleeing thief; a decision which would in the future haunt him.A few days later, Parker arrived at his Queens, 
			New York home to discover that his uncle Ben had been killed by a burglar. Distraught by the death of his beloved 
			guardian he vowed to capture the criminal. Easily apprehended the killer, Parker recognized the criminal as the thief 
			he allowed to escape at the television studio. Realizing that his inaction inadvertently doomed his uncle, Parker took 
			to heart the advice that with great power comes great responsibility. Vowing to honor Uncle Ben's memory Parker pledged 
			his life to fighting crime and injustice. Armed with his superhuman abilities and scientific genius the reluctant superhero 
