			circus where she worked as a fortuneteller; a cover for her activities as a sorceress. The circus performers, who 
			harbored no prejudices against so-called "freaks" adopted Wagner and Margali acted as his foster mother.</p><br>
			
			<p>A Texas millionaire, who wanted to cast Wagner in a freak show, bought the Bavarian circus. The young mutant quit 
			and fled to the German village of Winzeldorf, where his foster brother Stefan was living.</p><br>
			
			<p>Wagner discovered his foster brother had gone mad and had slain several local children. Wagner tracked Stefan 
			and fought him, hoping to end his rampage. During the struggle, Wagner unintentionally broke Stefan's neck. 
			Discovered by the villagers Wagner was assumed to be a demon responsible for the child killings. They were about 
			to kill him when <a href="ProfessorX.php" target="_self">Professor Charles Xavier</a> intervened, psionically 
