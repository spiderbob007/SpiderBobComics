			<p>Before America's entry into World War II, American reporter Keen Marlow traveled to Germany to investigate the Nazi 
			government. Arrested as a spy, Marlow was imprisoned and tortured. While incarcerated, Marlow met Dr. Eric Schmitt, 
			creator of a super-soldier serum. Consuming the formula, Marlow gained superhuman strength and escaped, using his new 
			powers to battle the Nazis as the <span class="bold">Mighty Destroyer</span>.</p>
			
			<p>While Marlow was active in Germany, British pacifists Brian Falsworth and Roger Aubrey traveled to Germany seeking 
			appeasement between the two nations. Arrested as spies, Falsworth and Aubrey were experimented upon by the Nazis. The 
			experiments upon Aubrey resulted in his transformation into a man less than one foot tall known as 
			<span class="bold">Dyna-Mite</span>. Falsworth was exposed to the same formula as Marlow, granting him superhuman 
			strength.</p><br>
						
			<p>After escaping the Nazis, Falsworth assumed the identity of the Destroyer, when Marlow returned to the United States. 
			When Aubrey escaped the brainwashing of the Nazis he rejoined his friend, who had assumed his father's former identity 
			of <a href="UnionJack.php" target="_self">Union Jack</a>, sharing the chores of fighting against their former captors as 
