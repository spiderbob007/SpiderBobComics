			<p>The powers of the techno-mage Lupex caused his body to wither and decay prematurely, forcing him to constantly 
			find refuge for his spirit in new bodies, but as his powers grew the process of decay accelerated for each subsequent 
			host body.</p><br>
			
			<p>On the outer fringes of the universe on the planet Styrakos, Lupex created a cybernetic host that could resist 
			the forces of decay brought about by his use of the dark arts. About the same time Lupex fostered an amorous 
			relationship with the sorceress Pyra, who was using him to steal his secrets. She became increasingly repulsed with 
			Lupex because of his decaying body and secretly took another lover, Klu. When Lupex learned of Pyra's betrayal he 
			possessed the body of Klu - forever denying her the one man she ever truly loved. In revenge Pyra programmed Lupex's 
			cyborg to rebel against him, but before he could subdue and possess the mechanoid it was stolen by the seventh 
			incarnation of the Timelord known as the Doctor - who increased the size of the cyborg and sent him into the future 
			as the freelance peace-keeping agent called <span class="bold">Death's Head</span>.</p><br>
			
			<p>After multiple adventures through space and time, Death's Head established himself on the Earth of the late 
			20<sup>th</sup> Century. In the not-to-distant future the terrorist organization 
			<a href="../../teams/marvel/AIM.php" target="_self">A.I.M.</a> predicted that an unknown entity would soon destroy 
			them and in an effort to challenge this unknown threat they created the cyborg Minion, who was programmed to travel 
			through space and time assimilating and killing 106 individuals. Individual #105 was Death's Head and #106 was 
			<a href="MrFantastic.php" target="_self">Reed Richards</a> of the 
			<a href="../../teams/marvel/FantasticFour.php" target="_self">Fantastic Four</a>.</p><br>
			
			<p>Minion succeeded in destroying Death's Head, but before it could complete its directive with Richards, 
			circumstances allowed the freelance peace-keeping agent to supplant the cyborg's programming and become the dominant 
			personality dubbed <span class="bold">Death's Head II</span>.</p><br>
			
			<p>Decades in the past, a legion of Death's Head androids were pulled into the Fornax galaxy to the planet Sakaar. 
			The legion's leader was destroyed by Sakaar's emperor. Leaderless, the remaining androids pledged allegiance to 
			the emperor, joining in his war against the parasitic Spikes. Immune to the Spikes’ ability to assimilate organic 
			matter the Death's Head androids were instrumental in the emperor's victory. There are clues scattered throughout 
			history that indicate a tie with <span class="bold">Death's Head 3.0</span> to the the original Death's Head, but 
