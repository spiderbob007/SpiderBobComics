	<tr>
		<td class="white_cell">
			<p>A schoolteacher that had recently moved to the frontier, Carter Slade ran afoul of outlaws and was left for dead. 
			Found by Comanche Indians, Slade was nursed back to health by the medicine man Flaming Star. The aging shaman believed 
			Slade to be the champion the Comanche sky-spirit had revealed to him in a vision. Slade was given a chalk-white cowboy 
			outfit impregnated with the phosphorescent dust of a meteorite giving him superhuman powers as the 
			<span class="bold">Ghost Rider</span>.</p><br>
			
			<p>Creating a legend of a phantom horseman who rode the frontier dispensing justice, Slade was called by various other 
			names; Night Rider, Phantom Rider, the Galloping Ghost, the Haunted Horseman and He Who Rides the Night Winds. When 
			Carter Slade was killed by outlaws his sidekick Jamie Jacobs briefly took his place and suffered the same fate of death. 
			Slade's brother, a U.S. Marshal named Lincoln adopted the mantle and continued the quest for justice of the 
			<span class="bold">Ghost Rider</span>.</p><br>
			
			<p>In unrelated events, emancipated slave Reno Jones moved west with his childhood friend Kid Cassidy, the son of Jones' 
			dead slave master. Cassidy became increasing embittered by the South's loss of the Civil War and the loss of his personal 
			fortune. After Cassidy threatened Jones with a gun the former slave was forced to shoot down his childhood friend. Jones 
			eventually settled in Wonderment, Montana to start a family.</p><br>
			
			<p>Unknown to Jones, Cassidy survived and joined a band of white supremacists known as the Night Riders. The Night Riders 
			had been terrorizing Wonderment, prompting Jones to call upon other western heroes to defend the town, he himself donning 
			a white costume and dubbing himself the <span class="bold">Ghost Rider.</p><br>
			
			<p>Almost a century after Carter Slade donned the Ghost Rider costume his descendent Hamilton Slade became an heir to the 
			legacy.</p><br>
			
			<p>An archeologist interested in American Indian lore, Hamilton Slade was intrigued by his family tales of adventure in 
			the wild west. Leading a team of archaeologists into the Nevada dessert, Slade came upon the ancient resting place of 
			his ancestor in a subterranean cave beneath a Comanche burial mound. Exploring the site alone, Slade was startled by a 
			ghostly garb levitating from a burial urn. In a flash of blinding light, Slade was possessed by the spirit of the phantom 
			horseman and acquired the spectral powers of the <span class="bold">Ghost Rider</span>.</p><br>
