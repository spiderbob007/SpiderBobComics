			<p>Virtually no one knew that Blaire was a mutant. Her audiences assumed that her lighting effects were created through 
			technological means. Although Blaire had no intention of using her special powers to fight crime, time and again she 
			found herself using them to thwart criminal activities.</p><br>
			
			<p>Moving to the Los Angeles to further her career she formed a romantic and professional relationship with Hollywood 
			celebrity Roman Nekoboh. Nekoboh, who produced a motion picture featuring Blaire, persuading her to publicly reveal 
			she was a mutant. The revelation backfired, stirring renewed public hostility against mutants. Blaire was virtually 
			blacklisted in the entertainment industry, and had to perform primarily in nightclubs to continue her singing career.</p><br>
			
