		<td class="white_cell">
			<p>Before European settlers moved into the western territories that would become the United States, the indigenous 
			tribes struggled amongst themselves for dominance.</p><br>
			
			<p>Owayadata, the wolf-god and patron of the Cheyenne people took pity upon his worshipers, sending a portion of 
			his power and essence to the warrior Wildrun transforming him into the champion known as 
			<span class="bold">Red Wolf</span>.</p><br>
			
			<p>About a century later when America was expanding its borders west the United States Calvary came into conflict 
			with the native peoples. In one incident a regiment of cavalrymen massacred a Cheyenne tribe - an infant being the 
			sole survivor. Adopted and raised by a Caucasian couple he was given the name Johnny Wakeley.</p><br>
			
			<p>Years later Wakeley's adopted parents were killed by a group of renegade Cheyenne. Pursued by the Cheyenne, Wakeley 
			discovered the burial place of a former Red Wolf. Visited by Owayadata, Wakeley was offered the power to become a 
			peacemaker for both his adopted people and the people of his heritage as the <span class="bold">Red Wolf</span>.</p><br>
			
			<p>Thomas Thunderhead, the great-great grandson of Johnny Wakeley, never gave any credence to the story of his ancestor 
			being the old-west champion of justice Red Wolf, until he was haunted by the dream of being tied to the ground and attacked 
			by wolves. Suddenly waking from a his nightmare, Thunderhead discovered an actual wolf in his apartment bedroom. Later 
			discovering the artifacts of his ancestor, Wakeley sensed he had received these and the gifts of enhanced senses from the 
			wolf-god, Owayadata, giving him the ability to battle against injustice in the new-west as the 
			<span class="bold">Red Wolf</span>.</p><br>
			
			<p>The spirit of Owayadata once again imbued a young Cheyenne with the powers of the wolf. After fighting in a foreign war, 
			William Talltrees returned to his native land to help his family oppose a corrupt land dealer named Cornelius van Lunt 
			(Taurus). Van Lunt had the Talltrees family massacred and William was the sole survivor. Recalling the tales of his youth, 
			Talltrees donned a ceremonial garb kept by his tribe's spiritual leaders and called upon the wolf god Owayadata who 
			granted him the powers of the <span class="bold">Red Wolf</span>.</p><br>
