		<td class="white_cell">
			<p>The hybrid offspring of Princess Fen of Atlantis and an American seaman, Captain Leonard McKenzie.Contracted by Paul 
			Destiny, McKenzie's ship was searching for the lost Lemurian city rumored to house the mythical Helmet of Power. 
			Distrusting Destiny's intention for the crown, McKenzie planted explosives in the ice flows causing an avalanche to bury 
			the city, unaware <a href="../../locations/marvel/Atlantis.php" target="_self">Atlantis</a> lay beneath. The damage the 
			underwater city sustained prompted King Thakorr to send his daughter, Fen, to investigate.</p><br>
			
			<p>Fen was captured by the ship's crew and in a strange twist of fate, the princess fell in love with and married McKenzie 
			at sea. Failing to return after several weeks, Thakorr dispatched a rescue party for Fen. The ensuing scuffle apparently 
			killed McKenzie, and Fen returned to Atlantis. Nine months later Namor was born.</p><br>
			
			<p>Namor was raised with a hostile attitude toward surface-dwellers. When King Thakorr discovered men in diving suits 
			near Atlantis, he assumed they were advance scouts for an invasion force and ordered Namor to attack New York, where he 
			fought the android <a href="HumanTorchI.php" target="_self">Human Torch</a>. Shortly thereafter, Nazis attacked Atlantis, 
			forcing Namor to join the <a href="../../teams/marvel/Invaders.php" target="_self">Invaders</a>, then later the 
			All-Winners Squadron fighting alongside <a href="CaptainAmericaI.php" target="_self">Captain America</a>, the Human 
			Torch, Spitfire and myriad other heroes of the day.</p><br>
			
			<p>Both an ally and advisory of surface dwellers, he always stands as the sovereign and protector of Atlantis as the 
			avenging son, <span class="bold">Namor the Sub-Mariner</span>.</p><br>
