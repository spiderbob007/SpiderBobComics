			to England where his discoveries in the Savage Land made him the target of foreign agents. Hiding his son Parnival, 
			Lord Plunder returned to the Savage Land with his son Kevin. When Lord Plunder was killed by the primitive Man-Apes, 
			Kevin was soon to be doomed if not for the intervention of the saber-toothed tiger, Zabu. When Maa-Gor, the leader 
			of the Man-Apes, attempted to kill Zabu, the young Plunder used his father's rifle to shoot Maa-Gor. Along with Zabu, 
			Plunder fled into the "Place of Mist" a region held as taboo and feared by the Man-Apes. Growing into manhood in the 
