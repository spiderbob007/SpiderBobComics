			war game exercises.</p><br>
			
			<p>His body was salvaged for use in a covert military cyborg program called Project Alpha-Mech. Manning's brain, 
			nervous system and still-living tissue were transplanted into a synthetic body equipped with a mini-computer and 
			dubbed <span class="bold">Deathlok</span>.</p><br>
			
			<p>Unrelated to Luthor Manning, mercenary John Kelly was killed and his brain was transplanted into a cyborg 
			resembling the one constructed in Manning's reality. Kelly's brain was eventually removed from the Deathlok 
			cyborg and his mental engrams were uploaded into a construct known as <span class="bold">Siege</span>.</p><br>
						
			<p>The cyborg body used for Kelly was in turn transplanted with the brain of pacifist computer technician 
			Michael Collins. Only intended to serve as hardware to serve the Deathlok programming, Collins' brain usurped 
			control of the cyborg and apparently is doomed to a life as <span class="bold">Deathlok</span>.</p><br>
