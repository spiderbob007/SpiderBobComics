	<tr>
		<td class="white_cell">
			<p>British nobleman Lord James Montgomery Falsworth became a secret operative during World War I, clad in a 
			costume resembling the personification of Great Britain's fighting spirit, he was known as the 
			<span class="bold">Union Jack.</p><br>
			
			<p>Towards the end of the war he would encounter an enemy that would plague him and his family until the end of 
			his days, the vampire Baron Blood, who was murdering members of the British Parliament. Union Jack stabbed Blood 
			with a silver blade forcing the vampire to flee. At wars end Union Jack retired from active service, married and 
			started a family.</p><br>
			
			<p>On the eve of World War II, Lord Falsworth disagreed not only with Prime Minister Neville Chamberlain's policy 
			of appeasement toward Germany's Nazi regime, but with his son Brian's opinions on the matter. An isolationist and 
			pacifist, Brian and his father quarreled to the point that he left home. With his lover Roger Aubrey, Brian Falsworth 
			went to Germany and publicly supported appeasement of the Nazi government.</p><br>
			
			<p>When the war escalated the two attempted to leave, but were imprisoned. Aubrey was sent to the Institute of 
			Nazi Science where he was brainwashed and subjected to experiments that transformed him into a man less than a foot 
			tall known as <span class="bold">Dyna-Mite.</p><br>
			
			<p>Meanwhile in a Hamburg prison, Falsworth met a biochemist who had created a variant of the Super-Soldier Formula 
			that had created <a href="CaptainAmericaI.php" target="_self">Captain America</a>. Near death, the scientist gave 
			Falsworth a sample he had hidden in a flask to keep it from the Nazis. Drinking the contents of the flask Falsworth 
			gained enhanced abilities and escaped from the prison vowing to fight the Nazis within Germany as the 
			<a href="MightyDestroyer.php" target="_self"><span class="bold">Mighty Destroyer</span></a>.</p><br>
			
			<p>Back in England Lord Falsworth had come out of retirement as Union Jack, joining a team of allied champions known 
			as the <a href="../../teams/marvel/Invaders.php" target="_self">Invaders</a> to battle his old enemy Baron Blood. 
			Union Jack battled the vampire in a cave beneath the Falsworth estate, where Baron Blood hurled a massive boulder 
			on Union Jack, crushing his legs and leaving him permanently crippled. Managing to pry the boulder from his legs 
			it rolled into Baron Blood, knocking him back into a formation of stalagmites impaling the vampire and apparently 
			killing him.</p><br>
			
			<p>Attempting to rescue his son from Germany, Lord Falsworth joined with the brainwashed Dyna-Mite and his daughter 
			Jacqueline, who had become <a href="Spitfire.php" target="_self">Spitfire</a> after receiving a blood transfusion 
			from the android <a href="HumanTorchI.php" target="_self">Human Torch</a> to save her from a bite by Baron Blood.</p><br>
			
			<p>Captured by the Nazis they were rescued by the Mighty Destroyer, who revealed himself as Brian Falsworth. Father and 
			son reconciled with Brian adopting the identity of the <span class="bold">Union Jack.</p><br>
			
			<p>Roger Aubrey was later restored to his true height and memories and adopted the identity of the 
			<span class="bold">Mighty Destroyer.</p><br>
			
			<p>Later in the war, Adolph Hitler convinced the <a href="../../gods_monsters/marvel/Asgardians.php" target="_self">Asgardian</a> 
			thunder god <a href="Thor.php" target="_self">Thor</a> to aid the cause of Germany, the homeland of the descendants of 
			the Norse people. When he clashed with the Invaders, he nearly killed Union Jack with a blast of lightening from his 
			enchanted hammer Mjolnir. Discovering Hitler's true goals, Thor vowed to aid him no more and withdrew most of the 
			electricity from Union Jack's body, restoring his health and enabling him to channel electric bolts.</p><br>
			
			<p>Brian Falsworth would be a member of the Invaders until the end of the war and apparently died in an automobile 
			accident in the 1950's.</p><br>
			
			<p>The elderly Lord Falsworth had hoped his daughter's son Kenneth Crichton would carry on the tradition of Union Jack, 
			but he had no desire for adventure. The mantle would be taken up by Joseph Chapman, a friend of Kenneth's, when Baron 
			Blood would once again resurface.</p><br>
			
			<p>Aware that Baron Blood was his brother, John Falsworth, Lord Falsworth contacted his former Invader ally Captain America 
			to aid in destroying his brother's evil forever. Lord Falsworth planned on baiting the vampire by dressing as Union Jack, 
			but suffering a heart attack, Joseph Chapman disguised himself instead.</p><br>
			
			<p>Captain America and the new Union Jack battled Baron Blood, with Captain America using his 
			<a href="../../vehicles_devices/marvel/CaptainAmericaShield.php" target="_self">shield</a> to behead the vampire, ending his 
			life permanently. Lord Falsworth died quietly while watching his brother's remains being burned.</p><br>
			
			<p>Joseph Chapman would later be possessed by the spirit of the Pendragon granting superhuman abilities and enabling him 
			to carry on the tradition of the <span class="bold">Union Jack</span>.</p><br>
		</td>
