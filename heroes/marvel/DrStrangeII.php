			forces.</p><br>
			
			<p>The Ancient One freed Strange from Mordo's containment spell, revealing that he had known his student's treacherous 
			plans all along and desired to keep his enemy close. Peering into Strange's soul, the sorcerer saw a potential for 
			goodness and adopted him as his disciple.</p><br>
			
			<p>His studies complete, the former surgeon returned to his Greenwich Village home. Though the world at large believes 
