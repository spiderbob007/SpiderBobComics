	<tr>
		<td class="white_cell">
			<p>Gamma radiation induced mutation resulting in a variety of Hulk manifestations.</p><br>
			
			<p>All current manifestations of the Hulk possess superhuman strength enabling him to lift in excess of 100 tons. 
			His highest strength levels are obtained when enraged. The Hulk can lift only 70 tons in a calm state. He is also 
			able to leap vast distances, and has demonstrated a powerful healing factor and immunity to harsh environments and 
			all known diseases. The Savage Hulk has shown a capability for sensing ethereal entities such as ghost and astral 
			projections.</p><br>
			
			<p>Dr. Banner suffers from multiple personality disorder (MPD), due to a traumatic childhood. As a child Banner 
			witnessed the death of his mother at the hands of his father. Each personality manifest as an individual creature, 
			and the variety and number of personalities appear to be limitless.</p><br>
			
			<p>The first of the three personalities that manifest was the Selfish or <span class="bold">Grey Hulk</span>, also 
			known in later years as <span class="bold">Mr. Joe Fixit</span>. The Hulk under the alias of Mr. Fixit worked for 
			the Las Vegas mob as an enforcer.</p><br>
			
			<p><span class="bold">Note:</span> The Grey Hulk exist only due to a coloring problem with 
			<a href="../../issues/marvel/1stHulkII.php" target="_self"><em>Incredible Hulk</em> #1</a>. The color 
			would not print neutral and appeared as purple or black in some panels. The Hulk's color was changed to a more stable 
			green. The Grey Hulk as a character was later resurrected by writer/artist Al Milgrom in  
			<a href="../../issues/marvel/Grey_Hulk_Returns.php" target="_self"><em>Incredible Hulk</em> #324</a> (Oct. 1986).</p><br>
			
			<p>The second and most common personality is the Child or <span class="bold">Savage Hulk</span>. This personality sways 
			between petulant or violent belligerence to child-like naivety and a desire for acceptance. The Savage Hulk has on 
			occasion been controlled by the mind of Dr. Banner, but his anger would eventually lead to the Savage Hulk's personality 
			gaining dominance.</p><br>
			
			<p>The third personality was once believed to be an amalgamation of the first two controlled by the rationality of Dr. 
			Banner. Although similar in personality, <span class="bold">Professor Hulk</span> has traits that are independent from the 
			first two manifestations and Dr. Banner. In cases of uncontrolled rage Professor Hulk reverts back to Dr. Banner, but 
			with the Savage Hulk's personality. This has been explained as a sub-conscious safety measure.</p><br>
			
			<p>Dr. Banner and Professor Hulk possess an above average intelligence and an aptitude in a wide variety of sciences, 
			especially physics and chemistry.</p><br>
			
			<p>The least common Hulk manifestations are the Maestro and the 
			<a href="../../villains/marvel/DevilHulk.php" target="_self">Devil-Hulk</a>.</p><br>
			
			<p>The <a href="../../villains/marvel/Maestro.php" target="_self">Maestro</a> is a future version of the Professor Hulk 
			personality. This possible version is a cruel dictator that has subjugated most of the world.</p><br>
			
			<p>The <a href="../../villains/marvel/DevilHulk.php" target="_self">Devil-Hulk</a> is the expression of Dr. Banner's 
			purest rage and hatred, his suppressed id. The Devil-Hulk has a striking resemblance to Dr. Banner's childhood perceptions 
			of his abusive father.</p><br>
			
			<p><span class="bold">Note:</span> Dr. Banner's transformation into the Hulk was initially triggered by the onset of night. 
			During times of emergency he would trigger the transformation by exposing himself to additional doses of Gamma Radiation. 
			These additional exposures appear to be responsible for his transformation being triggered by stress as opposed to the 
			phases of the day.</p><br>
