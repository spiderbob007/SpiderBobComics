			<p><span class="bold">Bucky I</span></p>
			<p>According to U.S. government propaganda, the orphaned James "Bucky" Barnes was the mascot of Camp Lehigh in 
			Virginia where he befriended Army Private Steve Rogers, who was secretly battling espionage agents and saboteurs as 
			<a href="CaptainAmericaI.php" target="_self">Captain America</a>. One night, Barnes discovered Rogers while changing 
			into his Captain America uniform. Swearing the youth to secrecy, Barnes was allowed to assist Captain America in his 
			mission against the <a href="../../villains/marvel/RedSkull.php" target="_self">Red Skull</a>. Barnes later received 
			permission and training to become Captain America's full-time partner, simply going by the name 
			<span class="bold">Bucky</span>.</p><br>
			
			<p>In reality James Barnes was older than the government propaganda depicted. In his late teens, Barnes was already an 
			exceptional fighter with no formal military training. Constantly getting into fights and landing into trouble with 
			the military justice system, the commanding officer of Barnes' deceased father offered the young scrapper an 
			opportunity to fight. Trained in special forces combat, Barnes was partnered with America's super soldier, 
			Captain America.</p><br>
			
			<p><span class="bold">Bucky II</span></p>
			<p>After the death of the original <a href="WinterSoldier.php" target="_self">Bucky</a> and the apparent death of 
			<a href="CaptainAmericaI.php" target="_self">Captain America</a>, who was attempting to stop a plot by the Nazi 
			commander <a href="../../villains/marvel/BaronZemoI.php" target="_self">Baron Zemo</a>, the US government recruited 
			replacements for the heroes to ensure the high moral of the public for the war effort. Professional baseball player 
			Fred Davis answered the call and served his nation as <span class="bold">Bucky</span> until his retirement after 
			suffering a gunshot wound.</p><br>
