			<p>Jenkins joined members of other incarnations of the <span class="bold">Masters of Evil</span> under the leadership 
			of <a href="../../villains/marvel/BaronZemoII.php" target="_self">Baron Zemo</a> as <span class="bold">MACH-1.</span> 
			Their plan was to deceive the public and government into believing they were a hero team known as the 
			<a href="../../teams/marvel/Thunderbolts.php" target="_self">Thunderbolts</a> to gain access to top-secret information 
			from <a href="../../teams/marvel/SHIELD.php" target="_self">S.H.I.E.L.D.</a> and the 
			<a href="../../teams/marvel/Avengers.php" target="_self">Avengers.</a></p><br>
			
