			brokered by <a href="Hawkeye.php" target="_self">Hawkeye</a>, Jenkins was incarcerated for a murder he committed in 
			his criminal past. While in prison Jenkins was forced to attack his former teammates through a cybernetically 
			controlled <span class="bold">Beetle</span>.</p><br>
			
			<p>Escaping prison, Jenkins returned to his former teammates in the Thunderbolts assuming an alias and the identity of 
			<span class="bold">MACH-2</span>.</p><br>
			
			<p>After the Thunderbolts were disbanded by the government Jenkins was forced to retire, but he soon returned with the 
			aid of the global anti-terrorist agency known as the V-Battalion under the updated armor known as 
			<span class="bold">MACH-3</span>.</p><br>
			
			<p>Jenkins volunteered to return to finish his prison sentence - fulfilling his debt to society. When he was legally 
			released he pledged to recreate the Thunderbolts - donning the next generation of his armor as 
