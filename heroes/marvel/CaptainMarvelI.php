		<td class="white_cell">
			<p>A race of humanoid aliens, the militaristic Kree had built a empire spanning the galaxy. Lead by a powerful psionic 
			automaton collective of great minds known as the Supreme Intelligence, the Kree seemed to know no boundaries, although 
			the Supreme Intelligences recognized the race was coming to an evolutionary dead-end, and began taking steps to rectify 
			this dilemma.</p><br>
			
			<p>Encouraging breeding with other humanoid species, the original Kree race diverted into two similar but different 
			races, the original blue skinned race and the new Caucasian skinned race called whites, which were biologically 
			similar to Earthlings. Many within the Kree race thought of these new whites as inferior and unworthy of life.</p><br>
			
			<p>After a Kree military defeat by the <a href="../../teams/marvel/FantasticFour.php" target="_self">Fantastic Four</a>, 
			the Supreme Intelligence begins to warrant the backwater planet known as Earth being worthy of it attention. Failing to 
			infuse vitality into the Kree gene pool the Supreme Intelligence turned its attention to the genetic potential of 
			Earthlings. It sets out to rearrange a single human's genetic structure and awaken their vast psionic ability - creating, 
			in essence, a harbinger of human evolution.</p><br>
			
			<p>Unlike Kree minds, the Supreme Intelligence was unable to directly absorb Earthlings' minds. Designing to merge a 
			human psyche to that of a biologically similar white Kree, the Supreme Intelligence would form a mental link to the human, 
			using the Kree as a proxy. If the plan worked, the Supreme Intelligence's creation would immediately gain limitless psionic 
			power. The Supreme Intelligence would then use the merged entity to destroy Earth, denying other alien races the ability 
			to exploit the inhabitants in such a manner.</p><br>
			
			<p>Secretly setting its scheme into motion, the Supreme Intelligence stationed candidates as spies on Earth. One spy is a 
			white Kree captain named Mar-Vell. He assumes the identity of chief scientist Dr. Walter Lawson at the Cape Canaveral 
			missile complex, under the watchful eye of the security commander Carol Danvers, the future 
			<a href="MsMarvel.php" target="_self">Ms. Marvel</a>.</p><br>
			
			<p>In time he develops a sympathy for the people of Earth and battles threats on their behalf. He also battles against 
			his Kree allies, who were plotting to overthrow the Supreme Intelligence. In a battle against his commander and murderer 
			of his love Una, Colonel Yon-Rog, Mar-Vell is trapped in the antimatter universe existing alongside Earth known as the 
			Negative Zone.</p><br>
			
			<p>Desiring to fulfill his plan to evolve the Kree race, the Supreme Intelligence telepathically enabled Mar-Vell to 
			contact the sometimes sidekick of the <a href="HulkII.php" target="_self">Hulk</a> and 
			<a href="CaptainAmericaI.php" target="_self">Captain America</a>, named Rick Jones. Mar-Vell telepathically directs Jones 
			to an abandoned Kree base on Earth, instructing the teen to don a pair of golden bracelets. Striking the extraterrestrial 
			Nega-Bands together, his atoms trade places with those of the dimensionally displaced Mar-Vell. The hero materializes on 
			Earth, and Jones is shunted to the Negative Zone safe within a protective aura, but maintaining his mental link to Captain 
			Marvel. Unknown to both the Supreme Intelligence had fulfilled the first part of its plan by created a bond between an 
			Earthling and a white Kree.</p><br>
			
			<p>Mar-Vell would later gain his ability of cosmic awareness from a galactic entity monitoring human evolution known as 
			Eon, in a bid to stop the mad Titan, <a href="../../villains/marvel/Thanos.php" target="_self">Thanos</a>.</p><br>
			
			<p>The legendary Kree warrior wielded his cosmic powers in defense of an entire galaxy. In the end, as part of a plot by 
			Nitro, he was stricken by the one enemy he could not defeat; cancer.</p><br>
			
			<p>Not on the field of battle, but on a sickbed - surrounded by an awesome assemblage of adventurers, gathered from across 
			the galaxy to pay tribute to the legacy of their fallen comrade, so died the hero called 
			<span class="bold">Captain Marvel</span>.</p><br>
