			teen he attended Lee School for Boys. It was while at Lee that he met history teacher "Steve Rogers."</p><br>
			
			<p>Both Monroe and the man calling himself Steve Rogers shared an admiration for 
			<a href="CaptainAmericaI.php" target="_self">Captain America</a>, who had disappeared during the final days of World 
			War II. Before taking the teaching position at Lee, Rogers had discovered in a spy's journal the notes for the Super 
			Soldier formula. Presenting a plan to the government to revive the symbol of liberty during the Korean War, the 
			program was accepted long enough for Roger's to alter his appearance with plastic surgery, but the war ended before he 
			could don the costume.</p><br>
			
			<p>When a communist imposter began a reign of terror as the 
			<a href="../../villains/marvel/RedSkull.php" target="_self">Red Skull</a>, both Monroe and Rogers injected the Super 
			Soldier Serum and donned costumes as Captain America and <span class="bold">Bucky</span>.</p><br>
			
			<p>The duo battled communist threats during the 1950's but because the formula had not been activated with Vita-Rays 
			it began to have a detrimental effect on the patriotic duos' mental health. Their paranoia causing them to attack 
			Communist's threats that did not exist, the cold-war era Captain America and Bucky were apprehended and placed into 
			suspended animation.</p><br>
			
			<p>Decades later the two heroes were released from their coma-like sleep by a Communist sympathizer, and unleashed 
			upon the revived Captain America and his partner the <a href="Falcon.php" target="_self">Falcon</a>. The two were 
			defeated and returned to suspended animation.</p><br>
			
			<p>The sleeping Monroe and Rogers were later acquired by the criminal psychologist Dr. Faustus who used them as test 
			subjects for his mind-controlling chemicals. Before Faustus was defeated, Rogers perished. Free of Faustus' mind 
			control Monroe was remanded to <a href="../../teams/marvel/SHIELD.php" target="_self">S.H.I.E.L.D.</a> custody where 
			he was treated for his paranoia.</p><br>
			
			<p>After Monroe was cured and released he sought out the original Captain America, who allowed him to use his 
			short-time identity of <span class="bold">Nomad</span>.</p><br>
			
			<p>After teaming with Captain America as Nomad, Monroe pursued a solo career - which lead to his apparent death. 
			In truth Monroe was placed back into suspended animation until he was revived as the mind-controlled assassin 
			<span class="bold">Scourge</span>.</p><br>
			
			<p>After Monroe was purged of the nanites which controlled his will as Scourge he received the devastating news 
			that he was slowly dying from his exposure to the Super Soldier Serum. In the time preceding his eventual death 
			he fought a one-man war against perceived evils as <span class="bold">Nomad</span>.</p><br>
