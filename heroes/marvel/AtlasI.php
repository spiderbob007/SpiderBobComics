		<td class="white_cell">
			<p>A mercenary, Eric Josten smuggled stolen technology to South America for the Nazi scientist Baron Heinrich Zemo 
			(<a href="../../villains/marvel/BaronZemoI.php" target="_self">Baron Zemo I</a>). With the equipment Josten smuggled, 
			Zemo created a form of mutagenic radiation known as ionic rays capable of bestowing super-human physical powers. 
			Zemo's first test subject was Simon Williams (<a href="WonderMan.php" target="_self">Wonder Man</a>), in a ploy to 
			infiltrate and destroy the <a href="../../teams/marvel/Avengers.php" target="_self">Avengers</a>.</p><br>
			
			<p>After Zemo was defeated he abandoned his South American hideout, which Josten retreated to after being wounded 
			by South American law enforcement. Mystically observed by Zemo's former 
			<a href="../../teams/marvel/Masters_of_Evil.php" target="_self">Masters of Evil</a> ally, the 
			<a href="../../gods_monsters/marvel/Asgardians.php" target="_self">Asgardian</a> 
			<a href="../../gods_monsters/marvel/Enchantress.php" target="_self">Enchantress</a>, she offered Josten great power 
			via the Ionic Ray machine. Emerging healed of his wounds and gaining super-human strength his benefactor dubbed him 
			<span class="bold">Power Man</span>.</p><br>
			
			<p>Power Man partnered with the Swordsman and the duo worked for a number of criminals including a brainwashed 
			<a href="BlackWidowII.php" target="_self">Black Widow</a>, 
			<a href="../../villains/marvel/RedSkull.php" target="_self">Red Skull</a>, 
			<a href="../../villains/marvel/MandarinI.php" target="_self">Mandarin</a> and the 
			<a href="../../villains/marvel/GrimReaper.php" target="_self">Grim Reaper</a>. It was with the Grim Reaper's 
			<a href="../../teams/marvel/LethalLegion.php" target="_self">Lethal Legion</a> that Josten would land in prison 
			for an extended period of time.</p><br>
			
			<p>After his incarceration, Josten learned that a hero named <a href="PowerManII.php" target="_self">Luke Cage</a> 
			had assumed his moniker. Challenging Cage, Josten was soundly defeated. His strength began to wane and disheartened 
			by his defeat, he resumed his former career of trafficking in illegal goods as the <span class="bold">Smuggler.</p><br>
			
			<p><a href="../../villains/marvel/CountNefaria.php" target="_self">Count Nefaria</a> offered Josten a position in 
			his criminal organization and renewal of his former powers. In fact Nefaria plotted to steal Josten and his other 
			henchmen's powers for himself. Escaping, Josten returned to his smuggling on the docks until being defeated and 
			incarcerated by <a href="SpiderManI.php" target="_self">Spider-Man</a>.</p><br>
			
			<p>While in prison Josten learned of a criminal scientist on the West Coast that specialized in superhuman powers. 
			After release Josten traveled to Los Angeles and made a bargain to regain his lost strength. Subjected to an extract 
			of the growth formula created by <a href="DrPym.php" target="_self">Dr. Henry Pym</a>, the Pym Particles reacted with 
			the remaining Ionic energy granting the ability to increase not only in strength but size and mass. Satisfied to steal 
			a superheroes name as his former name had been taken, Josten dubbed himself <span class="bold">Goliath.</p><br>
			
			<p>Goliath joined <a href="../../villains/marvel/BaronZemoII.php" target="_self">Baron Helmut Zemo</a>'s 
			<a href="../../teams/marvel/Masters_of_Evil.php" target="_self">Masters of Evil</a> in the notorious attack upon 
			Avenger's Mansion. Years later after the events when Earth's heroes battled 
			<a href="../../villains/marvel/Onslaught.php" target="_self">Onslaught</a>, Zemo would once again organize the 
			Masters of Evil disguised as a group of superheroes called the 
			<a href="../../teams/marvel/Thunderbolts.php" target="_self">Thunderbolts</a>, with Josten taking the codename 
			<span class="bold">Atlas</span>.</p><br>
