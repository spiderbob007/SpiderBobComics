			was attempting repairs when a strange armored man came crashing through the jungle.</p><br>
			
			<p>At first skeptical of the strange <a href="IronManI.php" target="_self">Iron Man</a>, Rhodes was convinced he was 
			on the side of the United States when he fought off a platoon of enemy troops.</p><br>
			
			<p>Allowing Iron Man to drain the downed helicopters batteries for power, the pilot and Iron Man trekked through the 
			jungle until they discovered a hidden enemy rocket base. Stealing a helicopter and destroying the base, they flew to 
			an American base.</p><br>
			
			<p>Tony Stark (<a href="IronManI.php" target="_self">Iron Man I</a>) offered Rhodes a job as a pilot when his tour 
			of duty expired. After a few years back from the war, Rhodes contacted Stark and was hired as his personal pilot, 
			bodyguard and chief aviation engineer.</p><br>
			
			<p>When personal problems and alcoholism rendered Tony Stark useless as Iron Man, Rhodes was forced to don the armor 
			to defend Stark International from the criminal Magma. Stark insisted Rhodes take fulltime responsibility as 
			<span class="bold">Iron Man</span> until he could recover from his alcoholism.</p><br>
			
			<p>Rhodes would serve as Iron Man until the armor began to cause medical problems. He would later return to service 
			with a new more lethal armor, designed specifically for him, known as the <span class="bold">War Machine</span>.</p><br>
