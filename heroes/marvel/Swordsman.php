		<td class="white_cell">
			<p>A master of bladed weapons Jacques Duquesne was famous as the circus performer called the 
			<span class="bold">Swordsman</span>. After incurring massive gambling debts Duquesne resorted to robbing his 
			employer to pay his creditors. Caught with the stolen money by his protégé Clint Barton 
			(<a href="Hawkeye.php" target="_self">Hawkeye I</a>) Duquesne gave pursuit which lead to the apparent death of 
			Barton.</p><br>
			
			<p>Fleeing the circus the Swordsman adopted the costumed identity of a criminal and mercenary in Europe. He 
			eventually returned to the states to apply for membership in the 
			<a href="../../teams/marvel/Avengers.php" target="_self">Avengers</a> as part of a plan to commit even more 
			spectacular crimes. His betrayal would eventually be revealed and the Swordsman would eventually become nothing 
			more than hired mussel for an Asian crimelord.</p><br>
			
			<p>While in Asia he encountered the woman he would love known as <a href="Mantis.php" target="_self">Mantis</a>. 
			His love for Mantis would lead to him sacrificing his life to save her from 
			<a href="../../villains/marvel/Kang.php" target="_self">Kang the Conqueror</a>.</p><br>
			
			<p>The Swordsman found a strange second chance at life when he was resurrected and became the human host for the 
			intelligent plant life known as the Cotati.</p><br>
			
			<p>Years after the death of the original <span class="bold">Swordsman</span> a new adventurer using the name 
			appeared. Hailing from an alternate dimension, Philip Jarvet apparently had a similar history and skills as 
			Duquesne, but never turned to crime. Jarvet was rescued and deceived by Proctor, the man who destroyed Jarvet's 
			world as well as many other alternate Earths in his quest to kill the Avengers member Sersi in all realities.</p><br>
			
			<p>Brainwashing Swordsman, and agents from other realties, Proctor orchestrated several attacks upon the Avengers. 
			Swordsman was captured by the Avengers and in time his mind cleared and he began to cooperate with the team. Proctor 
			attempted to use the <a href="VisionII.php" target="_self">Vision</a> of an alternate reality to kill Swordsman but 
			he failed and in the process lost another of his team members Magdelene - who had grown to love the Swordsman.</p><br>
			
			<p>After the Avengers disappeared during a battle with 
			<a href="../../villains/marvel/Onslaught.php" target="_self">Onslaught</a> both the Swordsman and Magdelene continued 
			their battle against evil. When the Avengers returned they helped them on a few occasions eventually leaving with the 
			<a href="../../teams/marvel/SquadronSupremeI.php" target="_self">Squadron Supreme</a> to their native reality.</p><br>
			
			<p>Some time later a new Swordsman appeared that came into conflict with both the heroic team known as the 
			<a href="../../teams/marvel/Thunderbolts.php" target="_self">Thunderbolts</a> and the terrorist organization 
			<a href="../../teams/marvel/HYDRA.php" target="_self">HYDRA</a>. In reality the new Swordsman is Andreas Strucker the 
			son of the Nazi supremacist Baron Wolfgang von Strucker. As children Andreas and his twin sister Andrea were 
			genetically altered by their father to give them superhuman abilities to generate powerful beams of energy, but their 
			power was dependent upon physical contact with one another. After 
			<a href="../../villains/marvel/BaronZemoII.php" target="_self">Baron Zemo</a>, who was in the guise of 
			<a href="CitizenV_IV.php" target="_self">Citizen V</a>, killed Andrea, Andreas was left powerless. Seeing an opportunity 
			to exploit the now powerless Andreas to strike at his enemies, Baron Zemo recruited Zebidiah Killgrave, the 
			<a href="../../villains/marvel/PurpleMan.php" target="_self">Purple Man</a>, to use his powers to manipulate and train 
			Andreas as the newest <span class="bold">Swordsman</span>.</p><br>
