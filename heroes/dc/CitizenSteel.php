			Henry Heywood III - creating the next generation to carry the mantle of <span class="bold">Commander Steel.</p><br>
			
	
			<p>Years after the deaths of both heroes to be called Commander Steel, their relative Nathan Heywood was attending a family 
			reunion, which was attacked by the Fourth Reich, a team of metahuman Neo-Nazis contracted by 
			<a href="../../villains/dc/VandalSavage.php" target="_self">Vandal Savage</a> to wipeout the bloodlines of Golden Age heroes. 
			Despite being crippled by a surgical leg amputation, Nathan Heywood battled to defend his family, attacking the villain Reichsmark 
			with his crutch. Reichsmark's wound spilled his liquid metal blood onto Heywood, which was absorbed into his skin transforming 
