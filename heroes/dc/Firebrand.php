			<p>Years later in unrelated events, New York City detective Alex Sanchez was mentally and physically traumatized. 
			No longer having a will to live he found purpose when the wealthy Noah Hightower outfitted him with a 
			cybernetic uniform able to project fiery energy bolts as <span class="bold">Firebrand</span>.</p><br>
			
