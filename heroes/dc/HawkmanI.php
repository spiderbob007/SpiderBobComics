			<p>Thousands of years ago, Egyptian Prince Khufu and his lover, came into contact with 
			<a href="../../vehicles_devices/dc/ThanagarianSpacecraft.php" target="_self">Nth</a> (ninth) metal, an element from the planet Thanagar. 
			Exposure to the extraterrestrial material imprinted racial memories from the alien 
			<a href="../../aliens_races/dc/Thanagarian.php" target="_self">Thanagarians</a> onto the couple and combined with their true love, strengthened 
			their souls and the bond between them.</p><br>
			
			<p>Soon after their exposure to Nth metal, the lovers were murdered by <a href="../../villains/dc/HathSet.php" target="_self">Hath-Set</a>, a 
			corrupt priest of Anubis. Over subsequent, and mostly unrevealed lifetimes, Khufu and Chay-Ara were reborn again and again, destined to meet and 
			fall in love.</p><br>
			
			<p>In the middle of the 20<sup>th</sup> century, Khufu was reborn as the independently wealthy Carter Hall, who after handling an Egyptian knife, realized his 
			true lineage and continued his quest for vengeance against his murderer Hath-Set, reborn as Anton Hastor. Utilizing the antigravity Nth metal to 
			construct an anti-gravity belt and wing harness in tribute to the Egyptian hawk god Horus, he adopted ancient weapons of war, becoming 
			<span class="bold">Hawkman</span>.</p><br>
