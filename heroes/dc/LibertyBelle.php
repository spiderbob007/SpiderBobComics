		<td class="white_cell">
			<p>A descendant of the American Revolutionary War heroine Miss Liberty, Libby Lawrence traveled Europe as her 
			father's secretary, Major James Lawrence. In Poland during the outbreak of World War II, Major Lawrence was killed 
			by a Nazi air raid. Libby Lawrence escaped and after surviving several adventures makes her way back across the 
			English Channel. In transport across the channel the ship was sunk and Lawrence swam toward British shores. 
			Hailed by Allied press as a miracle survivor, Lawrence became a celebrity. Returning to America, Lawrence became 
			a journalist and while on a trip to Liberty Hall, the home of the Liberty Bell in Pennsylvania, she was subject 
			to a mystical bond with the symbol of liberty which gives her the power to battle against tyranny as 
			<span class="bold">Liberty Belle.</p><br>
			
			<p>In her adventures as Liberty Belle with the <a href="../../teams/dc/JSA.php" target="_self">All-Star Squadron</a>, 
			Lawrence met and married Johnny Chambers, the speedster known as Johnny Quick. The superhero duo had a daughter 
			they named Jesse. Eventually the couple split over disagreements involved with their lives as costumed adventurers, 
			Jesse Chambers inherited aspects of both of her parents' powers and she followed in her father's footsteps as the 
			superfast <span class="bold">Jesse Quick</span>. After the death of her father, and reconciliation with her mother, 
			Jesse Chambers tapped into her mother's legacy as <span class="bold">Liberty Belle</span>.</p><br>
