		<td class="white_cell">
			<p><span class="bold">Sandman II</span></p>
			<p>A specialist in sleep research, Dr. Garrett Sanford invented a "Dream Monitor" which could read the dreams of the sleeping. 
			Summoned to the nations capital, Sanford learned that the President had become trapped in a coma-like sleep from which nothing 
			could wake him. Sanford's Dream Monitor was brought to Washington and linked to an experimental apparatus which was hoped could 
			enable a man to be projected physically into the Dream Dimension.</p><br>
	
			<p>Sanford entered the Dream Dimension gaining superhuman abilities. He was able to rescue the President from a nightmare 
			monster, but unable to physically return himself. The government sent Sanford's invention into the Dream Dimension in hopes 
			of him returning, and in time he was able to, but only for an hour at a time. Establishing himself within a Dream Dome, 
			Sanford used his new found abilities to protect humanity from the creatures of nightmare as the <span class="bold">Sandman</span>.</p><br>
	
			<p><span class="bold">Sandman III</span></p>
			<p>The son of Carter (<a href="HawkmanI.php" target="_self">Hawkman I</a><span>) and Shiera Hall 
			(</span> <a href="HawkgirlI.php" target="_self">Hawkgirl I</a>), Hector Hall was born in Cairo Egypt while his parents were on an 
			archaeological expedition. Unknown to his parents they had been cursed by Hath-Set, resulting in their child being born without a 
			soul. Hath-Set's plot was for the child to serve as a vessel for his agent of vengeance known as the Silver Scarab.</p><br>
			
			<p>As a young adult and apparently as a result of his body being possessed by the agent of Hath-Set, Hall created a solar 
			powered armor constructed of <a href="../../vehicles_devices/dc/ThanagarianSpacecraft.php" target="_self">Nth metal</a> 
			dubbing himself the <span class="bold">Silver Scarab</span>.</p><br>
			
			<p>Hall and other progeny of the <a href="../../teams/dc/JSA.php" target="_self">Justice Society</a> applied for membership in 
			the team, but were rejected by their elders and eventually formed their own team dubbed Infinity Inc. In time the curse of 
			Hall's possession would eventually take over his mind and he would die in the ensuing battle against his teammates.</p><br>
			
			<p>Without a soul, Hall's consciousness was cast into the realm known as the Dreaming. Two nightmares, Brute and Glob, desiring 
			to control the realm in the absence of its master <a href="../../gods_monsters/dc/SandmanIV.php" target="_self">Morpheus</a>, 
			convinced Hall to possess the now soulless body of the deceased Dr. Garrett Sanford becoming the new guardian of dreams known 
			as the <span class="bold">Sandman</span>.</p><br>
