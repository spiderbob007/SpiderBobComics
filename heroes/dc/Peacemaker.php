		<td class="white_cell">
			<p><span class="bold">Peacemaker I</span></p>
			<p>Born to an Austrian industrialist and an American author, Christopher Schmidt was raised in Europe.</p><br>
			
			<p>When he was very young Schmidt's father committed suicide when his history as a Nazi war criminal was revealed. Fleeing 
			the scandal and sorrow Schmidt's mother moved with her son back to America and changed their last name to Smith.</p><br>
			
			<p>Haunted by his father's Nazi past, Smith joined the U.S. military as a means to retribution. Earning the rank of Sergeant, 
			Smith was sent abroad to war. While in combat faulty intelligence lead to the massacre of a village. Scapegoated, 
			Smith was convicted and imprisoned in a military prison.</p><br>
			
			<p>Shortly after being imprisoned Smith's mother died leaving him a vast European fortune. Granted early reprieve for 
			participating in an anti-terrorist initiative, Smith used his fortune to create a private foundation based in Geneva 
			dedicated to battling terrorism as the <span class="bold">Peacemaker.</p><br>
			
			<p>Christopher Schmidt died liberating a South American country of Parador from the evil influence of 
			<a href="../../gods_monsters/dc/Eclipso.php" target="_self">Eclipso I</a>. His identity as Peacemaker was 
			presumably assumed by a government agent, whose identity remains a secret.</p><br>
			
			<p>Years later a new recruit, pulmonary surgeon, Mitchell Black, was sanctioned by the Geneva based project 
			as the head of its Enforcement Division - assuming the identity of the <span class="bold">Peacemaker</span>.</p><br>
