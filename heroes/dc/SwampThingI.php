			and later married his wife Linda. Suspecting his new wife had learned the truth of her deceased husband, he plotted to kill 
			her. Returning as the Swamp Thing, Olsen killed Ridge and saved his wife, although she was terrified by and rejected him. 
			Dejected Olsen took his place within the Parliament of Trees.</p><br>
			
			<p>The second conscious to become the Swamp Thing was Dr. Alec Holland.</p><br>
			
			<p>Doctors Alec and Linda Holland were working on a secret U.S. Government bio-restorative project in the Louisiana swamp. 
			The couple were offered a deal by a member of a criminal organization called the Conclave, named Ferrett. The Holland's 
			were not cooperative and Ferrett returned to kill them. To make the murder look accidental he rendered Alec unconscious 
			and planted a bomb in his lab. Holland regained consciousness but was caught in the explosion. Engulfed in flames he ran 
			into the swamp, but was dead before sinking into the muck. He would return from the swamp to seek revenge for his and his 
			wife's death.</p><br>
			
