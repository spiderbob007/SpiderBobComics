	</tr>
	<tr>
		<td class="white_cell">
			<p>Kent Nelson was born in 1928 to Sven Nelson, a noted archaeologist, and Celestine Babcock Nelson, a 
			spiritual medium. Celestine died under unknown circumstances shortly after Kent's birth, and Kent became a 
			tagalong for his globe-trotting father.</p><br>
			
			<p>In 1940, Sven and Kent were conducting a dig in the Valley of Ur in Mesopotamia when the younger Nelson 
			stumbled upon an ancient sarcophagus containing the giant body of the mystic, Nabu the Wise. Upon opening 
			the sarcophagus, Kent released poison gas which immediately killed his father.</p><br>
			
			<p>Nabu, a mystical Lord of Order originally from the planet Cilia, took pity on the boy and used his magic 
			to block the grief from the young boy's mind. Over the intervening months, Nabu schooled the boy in the ways 
			of mysticism, teaching him to cast spells and fly. During this time, Kent aged years instead of weeks and 
			had grown to adulthood by late summer.</p><br>
			
			<p>Kent had grown fond of Nabu, but the Lord of Order required one last act, his own death. Hesitant, Nabu 
			removed the inhibitions he had placed on his student's grief. Enraged by his father's death, Kent slew the 
			old wizard. His body destroyed, Nabu revealed his true nature: an energy being, a millennia-old Lord of Order. 
			Presenting Kent with two gifts: a helm and an amulet, he was joined with Nabu who used his body as an agent 
			against the Lords of Chaos, becoming <span class="bold">Dr. Fate</span>.</p><br>
			
			<p>On his first adventure as Dr. Fate, Kent Nelson rescued Inza Cramer from the warlock Wotan, and eventually 
			married her. By virtue of Nabu's talismans the couple retained their youth for decades - but eventually the 
			strains of battling the forces of chaos caused the spells to fail and the couple rapidly aged and died, 
			entering the spirit world within the Amulet of Anubis.</p><br>
			
			<p>Refusing to cede his battle against chaos, Nabu chose a new host for the Artifacts of Fate, the young boy 
			Eric Strauss who like Kent Nelson was transformed into an adult. Although now a man, Eric remained with his 
			step-mother Linda Strauss. While battling against an agent of chaos, Dr. Benjamin Stoner, who stole the 
			Strauss' mystical powers becoming an evil version of Dr. Fate, both step-mother and son mystically merged 
			and reclaimed the power of <span class="bold">Dr. Fate</span>.</p><br>
			
			<p>To guide the Strausses, Nabu resurrected Kent Nelson to serve as their mentor, but eventually both of 
			them met a mortal fate, but their souls lived on in others who had recently departed and were resurrected by 
			Nabu.</p><br>
			
			<p>Nabu eventually resurrected Inza Nelson, and like the Strausses, both she and Kent Nelson merged to form 
			Dr. Fate. In time Kent was no longer able to merge with his wife and she assumed the role as a new wholly 
			female <span class="bold">Dr. Fate</span>.</p><br>
			
			<p>Inza Nelson was a proactive Dr. Fate and prone to abuse her abilities. In time she would perish during an 
			event known as <a href="../../events/dc/ZeroHour.php" target="_self">Zero Hour</a>.</p><br>
			
			<p>After Inza Nelson's death the Artifacts of Fate came into the possession of grave robber named Jared 
			Stevens, who forged the artifacts into weapons which he used as the mercenary known only as 
			<span class="bold">Fate</span>.</p><br>
			
			<p>Meanwhile along a parallel tract the son of Carter (<a href="HawkmanI.php" target="_self">Hawkman I</a>) 
			and Shiera Hall (</span> <a href="HawkgirlI.php" target="_self">Hawkgirl I</a>), Hector Hall< was born in 
			Cairo Egypt while his parents were on an archaeological expedition. Unknown to his parents they had been 
			cursed by Hath-Set, resulting in their child being born without a soul. Hath-Set's plot was for the child 
			to serve as a vessel for his agent of vengeance known as the Silver Scarab.</p><br>
			
			<p>As a young adult and apparently as a result of his body being possessed by the agent of Hath-Set, Hall 
			created a solar powered armor constructed of 
			<a href="../../vehicles_devices/dc/ThanagarianSpacecraft.php" target="_self">Nth metal</a> 
			dubbing himself the <span class="bold">Silver Scarab.</p><br>
			
			<p>Hall and other progeny of the <a href="../../teams/dc/JSA.php" target="_self">Justice Society</a> applied 
			for membership in the team, but were rejected by their elders and eventually formed their own team dubbed 
			Infinity Inc. In time the curse of Hall's possession would eventually take over his mind and he would die 
			in the ensuing battle against his teammates.</p><br>
			
			<p>Without a soul, Hall's consciousness was cast into the realm known as the Dreaming. Two nightmares, 
			Brute and Glob, desiring to control the realm in the absence of its master 
			<a href="../../gods_monsters/dc/SandmanIV.php" target="_self">Morpheus</a>, convinced Hall to possess the 
			now soulless body of the deceased Dr. Garrett Sanford becoming the new guardian of dreams known as the 
			<a href="SandmanII.php" target="_self">Sandman</a>.</p><br>
			
			<p>When Morpheus returned to the realm of dreams he cast Hall into the realm of the dead, where he was 
			destined to be reborn as an agent of balance.</p><br>
			
			<p>Believing the spirit of Nabu had returned through a child, the dark lord 
			<a href="../../villains/dc/Mordru.php" target="_self">Mordru</a>, and his minions the Sons of Anubis, 
			plotted to steal the Artifacts of Fate. Killing the current agent of balance, Jared Stevens (Fate), 
			the artifacts mystically returned to the Tower of Fate.</p><br>
			
			<p>Abducting the newborn child Mordru was poised to take possession of the artifacts when the transformation 
			began. With the intervention of the Justice Society the transformation was complete and Hector Hall was reborn 
			as a new agent of balance, <span class="bold">Dr. Fate</span>.</p><br>
