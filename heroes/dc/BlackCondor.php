		<td class="white_cell">
			<p>Their caravan ambushed by Mongolian bandits, the archeologist parents of Richard Grey Jr. were killed and he was left an orphan 
			to the wilderness - where he was exposed to the radiation of a meteor, which caused a unique mutation. Adopted by a family of condors 
			he was raised as one of them, until he was found by a missionary named Father Pierre - who taught the child to speech and literature. 
			It wasn't until Pierre was murdered by the criminal Jaspar Crow that Grey used the missionary's nickname for him in the pursuit of 
			justice as the <span class="bold">Black Condor</span>.</p><br>
			
			<p>Decades later another would succeed Grey, when Ryan Kendall was experimented on by his grandfather in the pursuit of a flying man 
			to serve the secret group know as the Society of the Golden Wing. Kendall was irradiated while still a fetus which caused his mutation. 
			At the age of twenty-one Kendall fell into a coma for two years and when he awoke he flew into the thick Pine Barrens of New 
			Jersey eventually resurfacing as the <span class="bold">Black Condor</span>.</p><br>
			
			<p>In an unrelated dynasty, members of the Navajo tribe tell of a legacy of men granted the power of flight by Tocotl the mystical 
			Spider Woman. The legend was born on the Long Walk to reservations forced on the Indians by the United States Calvary - telling of 
			when condors would supposedly carry the souls of the dead back home to Canyon de Chelly. When the Navajo returned to their native 
			home they now had to face cattlemen who desired their land. Desperate, a young brave climbed the cliffs to beg the condors for 
			protection. At night he was transformed by Tocotl. Over a century later John Trujillo answered the call of the Spider Woman to 
			defend the land and her people as the <span class="bold">Black Condor</span>.</p><br>
