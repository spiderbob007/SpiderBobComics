			enlarge the city.</p><br>
			
			<p>Hunted like a fugitive, and lacking his normal superpowers within the bottle city due to its simulated red sun, Superman 
			decided to adopt a new identity patterned after his fellow crimefighter Batman. Because there were no bats or robins on Krypton, 
			Superman and Jimmy Olsen created the costumed identities of <span class="bold">Nightwing</span> and <span class="bold">Flamebird</span>.</p><br>
			
			<p>The duo returned to Kandor as Nightwing and Flamebird in times of emergency and occasionally were joined by 
			<a href="BatmanI.php" target="_self">Batman</a> and <a href="RobinI.php" target="_self">Robin</a>.</p><br>
			
			<p>Some time later Van-Zee, a Kandorian scientist, and his lab assistant Ak-Var assumed the guise of the crimefighters 
			<span class="bold">Nightwing</span> and <span class="bold">Flamebird.</p><br>
			
			<p><span class="bold">Post-Crisis</span></p>
			
			<p>Believing they were traveling to Krypton before its destruction in the past, Superman and 
