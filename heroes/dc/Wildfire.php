			<p><span class="bold">Pre-Zero Hour</span></p>
			<p>An astrophysics student at Metropolis University in the latter half of the 30<sup>th</sup> Century, Drake Burroughs was caught in an accident 
			in the research laboratory which converted his physical body into a sentient anti-energy form. Before his energy form dissipated, 
			the laboratory's chief scientist, Professor Vultan, captured Burroughs within a containment suit which allowed him to assume a 
			humanoid form and focus his new body into various energy forms.</p><br>
	
			<p>First adopting the name Erg-1 (Energy Release Generator 1), Burroughs applied for membership in the 
			<a href="../../teams/dc/LegionSuperHeroes.php" target="_self">Legion of Super-Heroes</a>, but was rejected because of his 
			similarity in powers to other members. Burroughs did not yet feel confident in his energy-based powers and the Legion assumed he 
			only had super-strength to offer. Eager to prove himself Burroughs secretly followed the Legion on a mission in which he saved 
			Colossal Boy by using his energy powers, but his inexperience caused him to disperse his entire form. Believing Erg-1 to be dead, 
			the Legion returned to Earth with his containment suit putting it on display at their headquarters, where Burroughs was able to 
			reclaim it and assume his place in the Legion as <span class="bold">Wildfire</span>.</p><br>
			
			<p><span class="bold">Post-Zero Hour</span></p>
			<p>Challenging the menace of the dark sorcerer <a href="../../villains/dc/Mordru.php" target="_self">Mordru</a>, Amazers member 
			Randall Burroughs (Atom'X) and Workforce member Jahr-Drake Ningle (Blast-Off II), both energy based heroes, were seemingly 
			destroyed. The two heroes' energy signatures were fused and when discovered by the 
			<a href="../../teams/dc/LegionSuperHeroes.php" target="_self">Legion</a> captured within a containment suit dubbed E.R.G. 1 
			(Energy Release Generator 1).</p><br>
	
			<p>After a battle with the <a href="../../aliens_races/dc/Blight.php" target="_self">Blight</a>, E.R.G. 1 and other Legionnaires 
			were transported across the galaxy through a tear in space - his containment suit destroyed. Unable to control his energy form 
			E.R.G. 1 coalesced into a star. Discovered by the by nomadic Kwai, the rogue star was lead back to the Legion and eventually 
			reconstituted within a new containment suit assuming the identity of <span class="bold">Wildfire</span>.</p><br>
