			for Donald Pierce, the renegade <a href="../../villains/marvel/DonaldPierce.php" target="_self">White Bishop</a> of the Inner Circle of the 
			<a href="../../teams/marvel/HellfireClub.php" target="_self">Hellfire Club</a>.</p><br>
		  
			<p>Xavier and his students tracked down Pierce, whose mercenaries were attempting to capture the young Brazilian mutant Robert da Costa 
			(<a href="../../heroes/marvel/Sunspot.php" target="_self">Sunspot</a>). Da Costa's girlfriend Juliana Sandoval was killed in the attempt.</p><br>
		  
			<p>Finally tracking and battling Pierce and his mercenaries, including a young mutant named Sam Guthrie 
			(<a href="../../heroes/marvel/CannonballII.php" target="_self">Cannonball II</a>) at Pierce's headquarters, he was defeated when Guthrie refused to kill 
			and turned against his mentor joining the other mutants.</p><br>
      
