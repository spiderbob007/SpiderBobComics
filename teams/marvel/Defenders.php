			<a href="../../heroes/marvel/HulkII.php" target="_self">Hulk</a> 
			to stop the sorcerer's his old enemy Yandroth from detonating the world’s nuclear arsenal; using the Omegatron - a super-computer 
			created by science and sorcery.</p><br>
		
			<p>The two powerhouses manage to breach the Omegatron's defenses allowing Dr. Strange's astral form to gain access to the computer. 
			Attempting to deactivate the super-computer, Strange discovers Yandroth's true plot was to use his comrades’ physical assault to 
			generate enough kinetic energy to activate the doomsday machine. Forced to divert his teammates against one another to stop their assault, 
			Strange cast a spell, freezing the Omegatron in time stasis moments before it could activate the world's nuclear arsenal.</p>
			
			<p>Coming closer to causing disaster than averting it, the group decides it would be best to part company. Though, eventually destiny 
