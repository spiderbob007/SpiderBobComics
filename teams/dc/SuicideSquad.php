		<td class="white_cell">
			<p>The first <span class="bold">Suicide Squad</span> was the informal name of an Army platoon during World War II reputed for its high 
			casualty loses and disaffected soldiers as willing to fight one another as they were the enemy. Things changed under the command of 
			<a href="../../heroes/dc/RickFlag.php" target="_self">Colonel Richard Flag</a>.</p><br>
			
			<p>The squad saw service in Korea, where Colonel Flag would lose his life serving his country. His son 
			<a href="../../heroes/dc/RickFlag.php" target="_self">Rick</a> 
			would be orphaned when his mother died in an auto accident. The young boy became the ward of General Stuart.</p><br>
		
			<p>Graduating from West Point, Colonel Rick Flag would use his skills as a pilot to join the space program. He would leave NASA to command 
			a newly formed Suicide Squad at the request of his guardian, General Stuart.</p><br>
			
			<p>Armed with the latest in cutting edge technology the Suicide Squad would battle threats from monsters and aliens to common criminals, 
			achieving worldwide acclaim for their exploits. The need for the squad would diminish with the proliferation of super-hero teams, and the 
			program's funding was discontinued.</p><br>
			
			<p>Years later the squad would find new purpose in the form of a covert special taskforce utilizing paroled metahuman criminals and super-villains. 
			Organized by Amanda "The Wall" Waller, an ambitious African-American woman that had worked her way from poverty to be a congressional aid. Waller 
			found reference to the Suicide Squad in government documents, and began to plan the formation of a new team.</p><br>
			
			<p>Colonel Flag was recruited as the field commander, with a small cell of trusted paroled super-villains to keep other recruits in line. 
			The team would operate for many years, but with the death of Flag and public exposure the team was again disbanded.</p><br>
			
			<p>Under the leadership of United States President, 
			<a href="../../villains/dc/LexLuthor.php" target="_self">Lex Luthor</a>, 
			a new squad was formed under the command of a man supposedly served during World War II, 
			<a href="../../heroes/dc/SergeantRock.php" target="_self">General Frank Rock</a>.</p><br>
