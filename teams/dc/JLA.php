			<p><a href="../../gods_monsters/dc/Starro.php" target="_self">Starro</a> 
			the Conqueror, a giant starfish from outer space is intent on conquering the Earth. 
			<a href="../../heroes/dc/Aquaman.php" target="_self">Aquaman</a> 
			issues the alarm after he witnesses Starro transform three ordinary starfish into powerful, atomic powered minions. The heroes meet and 
			split up to take care of the individual threats.</p><br>
			
			<p><a href="../../heroes/dc/GreenLanternII.php" target="_self">Green Lantern</a> 
			defeats the first starfish minion by using his power ring to will the starfish to return to normal, 
			<a href="../../heroes/dc/WonderWomanI.php" target="_self">Wonder Woman</a> and the 
			<a href="../../heroes/dc/MartianManhunter.php" target="_self">Martian Manhunter</a> 
			beat the second starfish using force, and the 
			<a href="../../heroes/dc/FlashII.php" target="_self">Flash</a> 
			best the third one by dropping a lake on it.</p><br>
		
			<p>The heroes meet teenager Snapper Carr, who mysteriously has become immune to Starro's hypno-ray, due to being covered in quicklime fertilizer 
			from working in the yard. The heroes dump barrels of lime onto Starro and the Earth is saved. Realizing they work more successfully as a 
			team the group creates a team like their predecessors in the 
			<a href="JSA.php" target="_self">Justice Society</a>. 
			For his contribution to the first victory, Carr is made an honorary member of the <span class="bold">Justice League</span>.</p><br>
