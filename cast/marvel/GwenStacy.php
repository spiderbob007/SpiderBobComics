			<p>The daughter of New York police Captain George Stacy, <span class="bold">Gwen Stacy</span> 
			attended Empire State University where she met and dated fellow student Peter Parker 
			(<a href="../../heroes/marvel/SpiderManI.php" target="_self">Spider-Man I</a>).</p><br>
			
			<p>Parker's identity as Spider-Man was unknown to Gwen, but apparently her father knew. 
			When he was mortally wounded by falling debris resulting from a battle between Spider-Man and 
			<a href="../../villains/marvel/DrOctopusI.php" target="_self">Dr. Octopus</a>, 
			he asked Spider-Man to take care of his daughter calling the masked vigilante by his name "Peter."</p><br>
			
			<p>Gwen like her father would become a victim of an enemy of Spider-Man. Discovering Spider-Man's true
			identity, the <a href="../../villains/marvel/GreenGoblinI.php" target="_self">Green Goblin</a> 
			struck against those that he loved most. Holding Gwen hostage atop the George Washington Bridge, 
			her unconscious body was knocked off the bridge by the Green Goblin when he battled Spider-Man. 
			Attempting to save her from the impact, Spider-Man caught her with his webbing before she hit the 
			water below. The sudden stop appears to have broken her neck, although the medical examiner
			declared her dead before the fracture.</p><br>
		  
			<p>Gwen's death caused Spider-Man's rage to almost murder the Green Goblin. Ironically his enemy would
			be responsible of his own apparent death when he was impaled by his Goblin Glider. The original Green Goblin 
			would return years later to continue menacing Spider-Man.</p><br>
		  
			<p>Spider-Man's former love briefly returned as a clone created by biology professor Dr. Miles Warren
			(<a target="_self" href="../../villains/marvel/Jackal.php" target="_self">Jackal</a>), the same villain responsible for 
			creating a clone of Peter Parker himself, who would also return from seeming death to claim the
			mantel of <a href="../../heroes/marvel/SpiderManII.php" target="_self">Spider-Man</a>.</p><br>
			
			<p>The whereabouts of Gwen Stacy's clone is unknown but presumed to be alive.</p><br>
